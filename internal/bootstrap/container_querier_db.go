// openReplicas opens all replica connections for a database registration and
// returns them as both a flat slice of *sql.DB (for health checks and cleanup)
// and a weighted pool of DBTX (for the balancer).
//
// Takes ctx (context.Context) for shutdown registration and logging.
// Takes name (string) which identifies the database for logging.
// Takes driverName (string) which is the database/sql driver name.
//
// Returns []*sql.DB which holds the opened replica connections.
// Returns []DBTX which holds the weighted pool for the balancer.
// Returns error when a replica connection cannot be opened or pinged.
func (*Container) openReplicas(
	ctx context.Context,
	name string,
	driverName string,
	registration *DatabaseRegistration,
) ([]*sql.DB, []DBTX, error) {
	_, l := logger_domain.From(ctx, log)

	var replicaConnections []*sql.DB
	var balancerPool []DBTX

	for replicaIndex, replica := range registration.Replicas {
		if ctx.Err() != nil {
			closeAllConnections(replicaConnections)
			return nil, nil, ctx.Err()
		}

		replicaDB, openError := sql.Open(driverName, replica.DSN)
		if openError != nil {
			closeAllConnections(replicaConnections)
			return nil, nil, fmt.Errorf("replica %d open: %w", replicaIndex, openError)
		}

		configureReplicaPool(replicaDB, registration, &replica)

		if pingError := replicaDB.PingContext(ctx); pingError != nil {
			_ = replicaDB.Close()
			closeAllConnections(replicaConnections)
			return nil, nil, fmt.Errorf("replica %d ping: %w", replicaIndex, pingError)
		}

		shutdown.Register(ctx, fmt.Sprintf("Database-%s-replica-%d", name, replicaIndex), func(_ context.Context) error {
			return replicaDB.Close()
		})

		replicaConnections = append(replicaConnections, replicaDB)

		weight := replica.Weight
		if weight <= 0 {
			weight = 1
		}
		for range weight {
			balancerPool = append(balancerPool, replicaDB)
		}

		l.Internal("Replica connection opened",
			logger_domain.String(logFieldDatabaseName, name),
			logger_domain.Int("replica", replicaIndex),
			logger_domain.Int("weight", weight))
	}

	return replicaConnections, balancerPool, nil
}

// closeAllInstances closes every database instance (primary + replicas),
// logging errors for primaries.
//
// Takes logger (logger_domain.Logger) which logs close errors.
// Takes instances (map[string]*databaseInstance) which holds the instances to
// close.
func closeAllInstances(logger logger_domain.Logger, instances map[string]*databaseInstance) {
	for name, instance := range instances {
		closeAllConnections(instance.replicas)
		if closeError := instance.db.Close(); closeError != nil {
			logger.Error("Failed to close database during rollback",
				logger_domain.String(logFieldDatabaseName, name),
				logger_domain.Error(closeError))
		}
	}
}

// closeAllConnections closes every connection in the slice, ignoring errors.
//
// Takes connections ([]*sql.DB) which holds the connections to close.
func closeAllConnections(connections []*sql.DB) {
	for _, connection := range connections {
		_ = connection.Close()
	}
}

// configureReplicaPool applies pool settings to a replica connection, using
// the replica's own values when non-zero and falling back to the primary's
// settings from the registration.
//
// Takes replicaDB (*sql.DB) which is the replica connection to configure.
// Takes registration (*DatabaseRegistration) which provides fallback pool
// settings from the primary.
// Takes replica (*Replica) which provides replica-specific pool overrides.
func configureReplicaPool(replicaDB *sql.DB, registration *DatabaseRegistration, replica *Replica) {
	maxOpenConnections := registration.MaxOpenConns
	if replica.MaxOpenConns > 0 {
		maxOpenConnections = replica.MaxOpenConns
	}
	if maxOpenConnections > 0 {
		replicaDB.SetMaxOpenConns(maxOpenConnections)
	}

	maxIdleConnections := registration.MaxIdleConns
	if replica.MaxIdleConns > 0 {
		maxIdleConnections = replica.MaxIdleConns
	}
	if maxIdleConnections > 0 {
		replicaDB.SetMaxIdleConns(maxIdleConnections)
	}

	connectionMaxLifetime := registration.ConnMaxLifetime
	if replica.ConnMaxLifetime > 0 {
		connectionMaxLifetime = replica.ConnMaxLifetime
	}
	if connectionMaxLifetime > 0 {
		replicaDB.SetConnMaxLifetime(connectionMaxLifetime)
	}

	connectionMaxIdleTime := registration.ConnMaxIdleTime
	if replica.ConnMaxIdleTime > 0 {
		connectionMaxIdleTime = replica.ConnMaxIdleTime
	}
	if connectionMaxIdleTime > 0 {
		replicaDB.SetConnMaxIdleTime(connectionMaxIdleTime)
	}
}
