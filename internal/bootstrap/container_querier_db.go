//nolint:revive // package-internal
func (c *Container) GetDatabaseService() (*databaseService, error) {
	c.querierDBOnce.Do(func() {
		c.createDatabaseService()
	})
	return c.querierDBService, c.querierDBErr
}

// GetDatabaseConnection returns the *sql.DB for a named database.
//
// Takes name (string) which identifies the database registered via
// AddDatabase.
//
// Returns *sql.DB which is the open database connection.
// Returns error when the database service cannot be created or the name is not
// registered.
func (c *Container) GetDatabaseConnection(name string) (*sql.DB, error) {
	svc, err := c.GetDatabaseService()
	if err != nil {
		return nil, fmt.Errorf("get database connection %q: %w", name, err)
	}

	inst, ok := svc.instances[name]
	if !ok {
		return nil, fmt.Errorf(errorFormatDatabaseLookup, name, ErrDatabaseNotRegistered)
	}
	return inst.db, nil
}

// GetDatabaseReader returns the DBTX for reading from a named database, where
// replicas are balanced via round-robin when configured or the primary
// connection is returned otherwise.
//
// Takes name (string) which identifies the database registered via
// AddDatabase.
//
// Returns DBTX which can execute read queries.
// Returns error when the database service cannot be created or the name is not
// registered.
func (c *Container) GetDatabaseReader(name string) (DBTX, error) {
	service, serviceError := c.GetDatabaseService()
	if serviceError != nil {
		return nil, fmt.Errorf("get database reader %q: %w", name, serviceError)
	}

	instance, exists := service.instances[name]
	if !exists {
		return nil, fmt.Errorf(errorFormatDatabaseLookup, name, ErrDatabaseNotRegistered)
	}
	return instance.reader, nil
}

// GetDatabaseWriter returns the DBTX for writing to a named database. When
