// createDatabaseService opens all registered database connections and builds
// migration services. Errors are stored in c.querierDBErr.
func (c *Container) createDatabaseService() {
	ctx := c.GetAppContext()
	_, l := logger_domain.From(ctx, log)

	if len(c.dbRegistrations) == 0 {
		l.Internal("No databases registered, skipping database service creation")
		c.querierDBService = &databaseService{
			instances: make(map[string]*databaseInstance),
		}
		return
	}

	l.Internal("Creating database service...",
		logger_domain.Int("count", len(c.dbRegistrations)))

	instances := make(map[string]*databaseInstance, len(c.dbRegistrations))

	for name, reg := range c.dbRegistrations {
		if ctx.Err() != nil {
			c.querierDBErr = ctx.Err()
			return
		}

		inst, err := c.openDatabaseInstance(ctx, name, reg)
		if err != nil {
			c.querierDBErr = fmt.Errorf(errorFormatDatabaseLookup, name, err)
			closeAllInstances(l, instances)
			return
		}
		instances[name] = inst
	}

	c.querierDBService = &databaseService{instances: instances}
	l.Internal("Database service created successfully",
		logger_domain.Int("connections", len(instances)))
}

// openDatabaseInstance opens a single database connection and optionally
// creates a migration service.
//
// Takes ctx (context.Context) for shutdown registration.
// Takes name (string) which identifies the database.
// Takes reg (DatabaseRegistration) which provides connection configuration.
//
// Returns *databaseInstance which holds the open connection and migration
// service.
// Returns error when the connection cannot be opened or the ping fails.
func (c *Container) openDatabaseInstance(
	ctx context.Context,
	name string,
	reg *DatabaseRegistration,
) (*databaseInstance, error) {
	database, err := c.acquireConnection(ctx, name, reg)
	if err != nil {
		return nil, err
	}

	shutdown.Register(ctx, "Database-"+name, func(_ context.Context) error {
		return database.Close()
	})

	instance := &databaseInstance{
		db:           database,
		reader:       database,
		writer:       database,
		driverName:   resolveDriverName(reg),
		replicaCount: len(reg.Replicas),
	}

	if checker, ok := reg.EngineConfig.Engine.(DatabaseHealthChecker); ok {
		instance.engineHealthChecker = checker
	}

	if err := c.attachReplicas(ctx, name, reg, database, instance); err != nil {
		return nil, err
	}

	if reg.EnableOTel {
		databaseSystem := resolveDBSystem(reg)
		instance.writer = newOTelDBTX(instance.writer, databaseSystem, name, reg.QueryNameResolver)
		instance.reader = newOTelDBTX(instance.reader, databaseSystem, name, reg.QueryNameResolver)
	}

	instance.migrator = c.createMigrationServiceForInstance(ctx, database, name, reg)
	instance.seeder = c.createSeedServiceForInstance(ctx, database, name, reg)

	return instance, nil
}

// acquireConnection returns a live *sql.DB for the registration, either
// reusing a pre-opened connection or opening a new one with pool settings.
//
// Takes ctx (context.Context) for ping verification.
// Takes name (string) which identifies the database for logging.
// Takes reg (*DatabaseRegistration) which provides connection configuration.
//
// Returns *sql.DB which is the verified database connection.
// Returns error when the connection cannot be opened or the ping fails.
func (*Container) acquireConnection(
	ctx context.Context,
	name string,
	reg *DatabaseRegistration,
) (*sql.DB, error) {
	if reg.DB != nil {
		return verifyPreOpenedConnection(ctx, name, reg.DB)
	}
	return openNewConnection(ctx, name, reg)
}

// verifyPreOpenedConnection pings a caller-supplied *sql.DB and returns it
// when reachable.
//
// Takes ctx (context.Context) for ping verification.
// Takes name (string) which identifies the database for logging.
// Takes database (*sql.DB) which is the pre-opened connection to verify.
//
// Returns *sql.DB which is the verified connection.
// Returns error when the ping fails.
func verifyPreOpenedConnection(ctx context.Context, name string, database *sql.DB) (*sql.DB, error) {
	if pingError := database.PingContext(ctx); pingError != nil {
		return nil, fmt.Errorf("ping pre-opened connection: %w", pingError)
	}

	_, l := logger_domain.From(ctx, log)
	l.Internal("Using pre-opened database connection",
		logger_domain.String(logFieldDatabaseName, name))

	return database, nil
}

// openNewConnection opens a fresh database connection using the registration's
// DSN and driver, applies pool settings, and verifies reachability with a ping.
//
// Takes ctx (context.Context) for ping verification.
// Takes name (string) which identifies the database for logging.
// Takes reg (*DatabaseRegistration) which provides DSN, driver, and pool
// settings.
//
// Returns *sql.DB which is the opened and verified connection.
// Returns error when no driver is configured, the connection cannot be opened,
// or the ping fails.
func openNewConnection(ctx context.Context, name string, reg *DatabaseRegistration) (*sql.DB, error) {
	driverName := reg.DriverName
	if driverName == "" {
		driverName = reg.EngineConfig.DriverName
	}
	if driverName == "" {
		return nil, ErrNoDriverConfigured
	}

	database, openError := sql.Open(driverName, reg.DSN)
	if openError != nil {
		return nil, fmt.Errorf("open: %w", openError)
	}

	applyPoolSettings(database, reg)

	if pingError := database.PingContext(ctx); pingError != nil {
		_ = database.Close()
		return nil, fmt.Errorf("ping: %w", pingError)
	}

	_, l := logger_domain.From(ctx, log)
	l.Internal("Database connection opened",
		logger_domain.String(logFieldDatabaseName, name),
		logger_domain.String("driver", driverName))

	return database, nil
}

// applyPoolSettings configures connection pool limits on the database when
// the registration specifies non-zero values.
//
// Takes database (*sql.DB) which is the connection to configure.
// Takes reg (*DatabaseRegistration) which provides pool settings.
func applyPoolSettings(database *sql.DB, reg *DatabaseRegistration) {
	if reg.MaxOpenConns > 0 {
		database.SetMaxOpenConns(reg.MaxOpenConns)
	}
	if reg.MaxIdleConns > 0 {
		database.SetMaxIdleConns(reg.MaxIdleConns)
	}
	if reg.ConnMaxLifetime > 0 {
		database.SetConnMaxLifetime(reg.ConnMaxLifetime)
	}
	if reg.ConnMaxIdleTime > 0 {
		database.SetConnMaxIdleTime(reg.ConnMaxIdleTime)
	}
}

// resolveDBSystem returns the OpenTelemetry db.system attribute value for the
// registration's driver, normalising common names to semantic convention values
// (e.g. "pgx" becomes "postgresql").
//
// Takes reg (*DatabaseRegistration) which provides driver configuration.
//
// Returns string which is the OTel db.system value.
func resolveDBSystem(reg *DatabaseRegistration) string {
	driver := reg.DriverName
	if driver == "" {
		driver = reg.EngineConfig.DriverName
	}
	switch driver {
	case "postgres", "pgx":
		return "postgresql"
	case "mysql":
		return "mysql"
	case "sqlite", "sqlite3":
		return "sqlite"
	case "duckdb":
		return "duckdb"
	default:
		return driver
	}
}

// resolveDriverName returns the raw database/sql driver name for the
// registration. Unlike resolveDBSystem, this preserves the original driver
// name without normalising to OTel semantic conventions.
//
// Takes reg (*DatabaseRegistration) which provides driver configuration.
//
// Returns string which is the driver name, or "pre-opened" when the caller
// supplies a pre-opened *sql.DB without engine configuration.
func resolveDriverName(reg *DatabaseRegistration) string {
	if reg.DB != nil {
		if reg.EngineConfig.DriverName != "" {
			return reg.EngineConfig.DriverName
		}
		return "pre-opened"
	}
	if reg.DriverName != "" {
		return reg.DriverName
	}
	return reg.EngineConfig.DriverName
}

// attachReplicas opens replica connections when configured and attaches them
// to the instance. Closes the primary connection on failure.
//
// Takes ctx (context.Context) for shutdown registration and logging.
// Takes name (string) which identifies the database for logging.
// Takes reg (*DatabaseRegistration) which provides replica configuration.
// Takes database (*sql.DB) which is the primary connection, closed on error.
// Takes instance (*databaseInstance) which receives the replica connections
// and balancer.
//
// Returns error when a replica connection cannot be opened or pinged.
func (c *Container) attachReplicas(
	ctx context.Context,
	name string,
	reg *DatabaseRegistration,
	database *sql.DB,
	instance *databaseInstance,
) error {
	if len(reg.Replicas) == 0 {
		return nil
	}

	replicaDriverName := reg.DriverName
	if replicaDriverName == "" {
		replicaDriverName = reg.EngineConfig.DriverName
	}

	replicaConnections, balancerPool, replicaError := c.openReplicas(ctx, name, replicaDriverName, reg)
	if replicaError != nil {
		_ = database.Close()
		return fmt.Errorf("replicas: %w", replicaError)
	}

	instance.replicas = replicaConnections
	instance.reader = &replicaBalancer{pool: balancerPool}

	return nil
}

// createMigrationServiceForInstance creates a migration service for a database
// instance if a migration filesystem is configured.
//
// Takes ctx (context.Context) for logging context.
// Takes database (*sql.DB) which is the database connection for migrations.
// Takes name (string) which identifies the database for logging.
//
// Returns querier_domain.MigrationServicePort which is the migration service,
// or nil when no migration filesystem is configured.
func (*Container) createMigrationServiceForInstance(
	ctx context.Context,
	database *sql.DB,
	name string,
	reg *DatabaseRegistration,
) querier_domain.MigrationServicePort {
	if reg.MigrationFS == nil {
		return nil
	}

	_, l := logger_domain.From(ctx, log)

	dialect := reg.MigrationDialect
	if reg.EngineConfig.MigrationDialect.CreateTableSQL != "" {
		dialect = reg.EngineConfig.MigrationDialect
	}

	directory := reg.MigrationDirectory
	if directory == "" {
		directory = "."
	}

	fileReader := migration_sql.NewFSFileReader(reg.MigrationFS)
	executor := migration_sql.NewExecutor(database, dialect)
	migrator := querier_domain.NewMigrationService(
		executor, fileReader, directory, reg.MigrationOptions...,
	)

	l.Internal("Migration service created",
		logger_domain.String(logFieldDatabaseName, name),
		logger_domain.String("directory", directory))

	return migrator
}

