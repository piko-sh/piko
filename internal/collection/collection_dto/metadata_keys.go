package collection_dto

const (
	// MetaKeyID is the metadata key for storing the item identifier.
	MetaKeyID = "ID"

	// MetaKeySlug is the metadata key for the URL slug of a content item.
	MetaKeySlug = "Slug"

	// MetaKeyLocale is the metadata key for the content item locale.
	MetaKeyLocale = "Locale"

	// MetaKeyTranslationKey is the metadata key for storing the translation key.
	MetaKeyTranslationKey = "TranslationKey"

	// MetaKeyURL is the metadata key for the URL used for page routing.
	MetaKeyURL = "URL"

	// MetaKeyTitle is the metadata key for the content title.
	MetaKeyTitle = "Title"

	// MetaKeyDescription is the metadata key for page descriptions.
	MetaKeyDescription = "Description"

	// MetaKeyDraft is the metadata key that shows whether content is a draft.
	MetaKeyDraft = "Draft"

	// MetaKeyTags is the metadata key for content tags.
	MetaKeyTags = "Tags"

	// MetaKeyWordCount is the metadata key for content word count analysis.
	MetaKeyWordCount = "WordCount"

	// MetaKeyReadingTime is the metadata key for the content reading time.
	MetaKeyReadingTime = "ReadingTime"

	// MetaKeySections is the metadata key for section headings in a document.
	MetaKeySections = "Sections"

	// MetaKeyCreatedAt is the metadata key for the creation timestamp.
	MetaKeyCreatedAt = "CreatedAt"

	// MetaKeyUpdatedAt is the metadata key for the content update timestamp.
	MetaKeyUpdatedAt = "UpdatedAt"

	// MetaKeyPublishedAt is the metadata key for the content publication date.
	MetaKeyPublishedAt = "PublishedAt"

	// MetaKeyNavigation is the metadata key for storing navigation data.
	MetaKeyNavigation = "Navigation"
)
