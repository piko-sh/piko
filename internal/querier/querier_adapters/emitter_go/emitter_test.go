func defaultMappings() *querier_dto.TypeMappingTable {
	return &querier_dto.TypeMappingTable{
		Mappings: []querier_dto.TypeMapping{
			{
				SQLCategory: querier_dto.TypeCategoryInteger,
				SQLName:     querier_dto.CanonicalInt8,
				NotNull:     querier_dto.GoType{Name: "int64"},
				Nullable:    querier_dto.GoType{Name: "*int64"},
			},
			{
				SQLCategory: querier_dto.TypeCategoryInteger,
				NotNull:     querier_dto.GoType{Name: "int32"},
				Nullable:    querier_dto.GoType{Name: "*int32"},
			},
			{
				SQLCategory: querier_dto.TypeCategoryText,
				NotNull:     querier_dto.GoType{Name: "string"},
				Nullable:    querier_dto.GoType{Name: "*string"},
			},
			{
				SQLCategory: querier_dto.TypeCategoryBoolean,
				NotNull:     querier_dto.GoType{Name: "bool"},
				Nullable:    querier_dto.GoType{Name: "*bool"},
			},
			{
				SQLCategory: querier_dto.TypeCategoryFloat,
				NotNull:     querier_dto.GoType{Name: "float64"},
				Nullable:    querier_dto.GoType{Name: "*float64"},
			},
		},
	}
}

func TestGoEmitterDelegateEmitQuerier(t *testing.T) {
	emitter := NewGoEmitter()

	file, err := emitter.EmitQuerier("mydb", 0)
	require.NoError(t, err)
	assert.Equal(t, "querier.go", file.Name)

	source := string(file.Content)

	fileSet := token.NewFileSet()
	_, parseError := parser.ParseFile(fileSet, "querier.go", source, parser.AllErrors)
	require.NoError(t, parseError, "generated code must be valid Go:\n%s", source)

	assert.Contains(t, source, "Code generated by Piko")
	assert.Contains(t, source, "package mydb")
	assert.Contains(t, source, "type DBTX interface")
	assert.Contains(t, source, "ExecContext")
	assert.Contains(t, source, "QueryContext")
	assert.Contains(t, source, "QueryRowContext")
	assert.Contains(t, source, "type Queries struct")
	assert.Contains(t, source, "func New(")
	assert.Contains(t, source, "func NewWithReplica(")
	assert.Contains(t, source, "func (queries *Queries) WithTx(")
	assert.Contains(t, source, "func (queries *Queries) RunInTx(")
}

func TestGoEmitterDelegateNoPgxOutput(t *testing.T) {
	emitter := NewGoEmitter()

	file, err := emitter.EmitQuerier("mydb", querier_dto.CapabilityBatch|querier_dto.CapabilityCopyFrom)
	require.NoError(t, err)

	source := string(file.Content)

	assert.NotContains(t, source, "PgxDBTX")
	assert.NotContains(t, source, "PgxQueries")
	assert.NotContains(t, source, "NewPgx")
	assert.NotContains(t, source, "pgx")
}

func TestGoEmitterDelegateEmitModels(t *testing.T) {
	emitter := NewGoEmitter()
	catalogue := &querier_dto.Catalogue{
		Schemas: map[string]*querier_dto.Schema{
			"": {
				Tables: map[string]*querier_dto.Table{
					"users": {
						Name: "users",
						Columns: []querier_dto.Column{
							{Name: "id", SQLType: querier_dto.SQLType{Category: querier_dto.TypeCategoryInteger}},
							{Name: "name", SQLType: querier_dto.SQLType{Category: querier_dto.TypeCategoryText}},
						},
					},
				},
			},
		},
	}

	files, err := emitter.EmitModels("db", catalogue, defaultMappings())
	require.NoError(t, err)
	require.Len(t, files, 1)

	source := string(files[0].Content)
	assert.Contains(t, source, "type Users struct")
}

func TestGoEmitterDelegateEmitQueries(t *testing.T) {
	emitter := NewGoEmitter()
	queries := []*querier_dto.AnalysedQuery{
		{
			Name:     "GetUser",
			Command:  querier_dto.QueryCommandOne,
			SQL:      "-- piko.name: GetUser\n-- piko.command: one\nSELECT id, name FROM users WHERE id = ?",
			Filename: "get_user.sql",
			OutputColumns: []querier_dto.OutputColumn{
				{Name: "id", SQLType: querier_dto.SQLType{Category: querier_dto.TypeCategoryInteger}},
				{Name: "name", SQLType: querier_dto.SQLType{Category: querier_dto.TypeCategoryText}},
			},
			Parameters: []querier_dto.QueryParameter{
				{Number: 1, Name: "id", SQLType: querier_dto.SQLType{Category: querier_dto.TypeCategoryInteger}},
			},
		},
	}

	files, err := emitter.EmitQueries("db", queries, defaultMappings())
	require.NoError(t, err)
	require.Len(t, files, 1)

	source := string(files[0].Content)

	fileSet := token.NewFileSet()
	_, parseError := parser.ParseFile(fileSet, "get_user.sql.go", source, parser.AllErrors)
	require.NoError(t, parseError, "generated code must be valid Go:\n%s", source)

	assert.Contains(t, source, "func (queries *Queries) GetUser(")
	assert.Contains(t, source, "QueryRowContext")
}

