// GeneratedFileHeader holds the standard header prepended to all generated Go files.
const GeneratedFileHeader = "// Code generated by Piko. DO NOT EDIT.\n\n"

// directiveCommentPrefix holds the SQL comment prefix used to identify directive lines.
const directiveCommentPrefix = "-- "

const (
	// orderByKeywordLength holds the character length of the "ORDER BY" keyword.
	orderByKeywordLength = len("ORDER BY")

	// decimalRadix holds the base-10 radix used for number parsing.
	decimalRadix = 10
)

// FormatFileWithAST builds a complete Go source file using goastutil.FormatAST
// which provides goimports processing.
//
// Takes packageName (string) which is the Go package name.
// Takes tracker (*ImportTracker) which holds collected import paths.
// Takes declarations ([]ast.Decl) which are the top-level declarations.
//
// Returns []byte which is the formatted Go source code with header prepended.
// Returns error when formatting fails.
func FormatFileWithAST(
	packageName string,
	tracker *ImportTracker,
	declarations []ast.Decl,
) ([]byte, error) {
	fileSet := token.NewFileSet()
	file := &ast.File{
		Name:  ast.NewIdent(packageName),
		Decls: declarations,
	}

	tracker.ApplyImports(fileSet, file)

	formatted, err := goastutil.FormatAST(fileSet, file)
	if err != nil {
		return nil, fmt.Errorf("formatting Go AST: %w", err)
	}

	var result bytes.Buffer
	result.Grow(len(GeneratedFileHeader) + len(formatted))
	_, _ = result.WriteString(GeneratedFileHeader)
	_, _ = result.Write(formatted)

	return result.Bytes(), nil
}

// StripDirectiveComments removes piko directive comment lines from a SQL
// string. Lines starting with "-- piko." or parameter directives like
// "-- ?1 as piko." / "-- :name as piko." are stripped so that the emitted
// SQL constant contains only the query itself.
//
// Takes sql (string) which is the raw SQL text possibly containing directives.
//
// Returns string which is the SQL with directive lines removed.
func StripDirectiveComments(sql string) string {
	var builder strings.Builder
	builder.Grow(len(sql))

	for line := range strings.SplitSeq(sql, "\n") {
		trimmed := strings.TrimSpace(line)
		if isDirectiveLine(trimmed) {
			continue
		}
		if builder.Len() > 0 {
			builder.WriteByte('\n')
		}
		builder.WriteString(line)
	}

	return strings.TrimSpace(builder.String())
}

// isDirectiveLine reports whether the given trimmed line is a
// piko directive comment that should be stripped from emitted
// SQL.
//
// Takes trimmed (string) which holds the whitespace-trimmed
// line to check.
//
// Returns bool which is true when the line matches a
// directive pattern.
func isDirectiveLine(trimmed string) bool {
	if strings.HasPrefix(trimmed, "-- piko.") {
		return true
	}
	if !strings.HasPrefix(trimmed, directiveCommentPrefix) || len(trimmed) <= len(directiveCommentPrefix) {
		return false
	}
	firstCharacter := trimmed[len(directiveCommentPrefix)]
	if firstCharacter == '$' || firstCharacter == '?' || firstCharacter == ':' || firstCharacter == '@' {
		return strings.Contains(trimmed, " as piko.")
	}
	return false
}

// RewriteNamedParameters replaces named parameter tokens (:name, @name, $name)
// in the SQL body with numbered ?N markers.
//
// Using numbered markers instead of bare ? ensures that reused named parameters
// (e.g. :id appearing twice) bind to the same positional argument.
//
// Takes sql (string) which is the SQL text with named parameter tokens.
// Takes parameters ([]querier_dto.QueryParameter) which provide the name-to-
// number mapping.
//
// Returns string which is the SQL with named parameters replaced by ?N.
func RewriteNamedParameters(sql string, parameters []querier_dto.QueryParameter) string {
	nameToNumber, found := buildNameToNumberMap(sql, parameters)
	if !found {
		return sql
	}

	var builder strings.Builder
	builder.Grow(len(sql))
	position := 0
	for position < len(sql) {
		character := sql[position]
		if character == '\'' {
			end := skipSQLStringLiteral(sql, position)
			builder.WriteString(sql[position:end])
			position = end
			continue
		}
		if character == ':' || character == '@' || character == '$' {
			end := rewriteNamedToken(sql, position, nameToNumber, &builder)
			if end > position {
				position = end
				continue
			}
		}
		builder.WriteByte(character)
		position++
	}
	return builder.String()
}

// buildNameToNumberMap constructs a map from parameter name
// to its 1-based positional number, and reports whether any
// named parameters appear in the SQL.
//
// Takes sql (string) which holds the SQL text to scan for
// named parameter tokens.
// Takes parameters ([]querier_dto.QueryParameter) which
// holds the parameter definitions with name-to-number
// mappings.
//
// Returns map[string]int which maps parameter names to their
// numbers.
// Returns bool which is true when the SQL contains at least
// one named parameter.
func buildNameToNumberMap(sql string, parameters []querier_dto.QueryParameter) (map[string]int, bool) {
	nameToNumber := make(map[string]int, len(parameters))
	hasNamedParameters := false
	for index := range parameters {
		parameter := &parameters[index]
		if parameter.Name == fmt.Sprintf("p%d", parameter.Number) {
			continue
		}
		nameToNumber[parameter.Name] = parameter.Number
		for _, prefix := range []string{":", "@", "$"} {
			if strings.Contains(sql, prefix+parameter.Name) {
				hasNamedParameters = true
				break
			}
		}
	}
	return nameToNumber, hasNamedParameters
}

// skipSQLStringLiteral advances past a single-quoted SQL
// string literal starting at position, handling escaped
// quotes (doubled single quotes).
//
// Takes sql (string) which holds the SQL text containing the
// string literal.
// Takes position (int) which holds the index of the opening
// single quote.
//
// Returns int which is the index just past the closing quote.
func skipSQLStringLiteral(sql string, position int) int {
	end := position + 1
	for end < len(sql) {
		if sql[end] == '\'' {
			end++
			if end < len(sql) && sql[end] == '\'' {
				end++
				continue
			}
			return end
		}
		end++
	}
	return end
}

// rewriteNamedToken rewrites a single named parameter token
// at the given position to a ?N placeholder, writing the
// result to the builder.
//
// Takes sql (string) which holds the SQL text containing the
// named parameter.
// Takes position (int) which holds the index of the prefix
// character (:, @, or $).
// Takes nameToNumber (map[string]int) which maps parameter
// names to their positional numbers.
// Takes builder (*strings.Builder) which receives the
// rewritten placeholder.
//
// Returns int which is the position after the token, or the
// original position if no named token was found.
func rewriteNamedToken(sql string, position int, nameToNumber map[string]int, builder *strings.Builder) int {
	if position+1 >= len(sql) || !isIdentStart(sql[position+1]) {
		return position
	}
	end := position + 2
	for end < len(sql) && isIdentPart(sql[end]) {
		end++
	}
	name := sql[position+1 : end]
	if number, found := nameToNumber[name]; found {
		fmt.Fprintf(builder, "?%d", number)
	} else {
		builder.WriteString(sql[position:end])
	}
	return end
}

// isIdentStart reports whether the byte is a valid first
// character of an identifier (letter or underscore).
//
// Takes character (byte) which holds the byte to check.
//
// Returns bool which is true for ASCII letters and
// underscore.
func isIdentStart(character byte) bool {
	return (character >= 'a' && character <= 'z') || (character >= 'A' && character <= 'Z') || character == '_'
}

// isIdentPart reports whether the byte is a valid
// continuation character of an identifier (letter, digit,
// or underscore).
//
// Takes character (byte) which holds the byte to check.
//
// Returns bool which is true for ASCII letters, digits, and
// underscore.
func isIdentPart(character byte) bool {
	return isIdentStart(character) || (character >= '0' && character <= '9')
}

// StripOrderByClause removes the ORDER BY clause from SQL text and trims
// trailing whitespace.
//
// Takes sql (string) which is the SQL text potentially containing an ORDER BY clause.
//
// Returns string which is the SQL without the ORDER BY clause.
func StripOrderByClause(sql string) string {
	upper := strings.ToUpper(sql)
	orderByIndex := strings.LastIndex(upper, "ORDER BY")
	if orderByIndex == -1 {
		return sql
	}

	rest := upper[orderByIndex+orderByKeywordLength:]
	endIndex := len(sql)
	for _, keyword := range []string{"LIMIT", "OFFSET", "FETCH", "FOR"} {
		keywordIndex := strings.Index(rest, keyword)
		if keywordIndex != -1 {
			endIndex = orderByIndex + orderByKeywordLength + keywordIndex
			break
		}
	}

	result := sql[:orderByIndex] + sql[endIndex:]
	return strings.TrimSpace(result)
}

// RenumberParametersExcluding rewrites $N and ?N placeholders in SQL to close
// gaps left by excluded parameter numbers.
//
// Takes sql (string) which is the SQL text containing placeholders.
// Takes excluded (map[int]bool) which holds the parameter numbers to exclude.
//
// Returns string which is the SQL with placeholders renumbered to close gaps.
func RenumberParametersExcluding(sql string, excluded map[int]bool) string {
	if len(excluded) == 0 {
		return sql
	}

	var builder strings.Builder
	builder.Grow(len(sql))
	position := 0

	for position < len(sql) {
		if sql[position] == '\'' {
			end := skipSQLStringLiteral(sql, position)
			builder.WriteString(sql[position:end])
			position = end
			continue
		}

		if isParameterPrefix(sql, position) {
			position = renumberPlaceholder(sql, position, excluded, &builder)
			continue
		}

		builder.WriteByte(sql[position])
		position++
	}

	return builder.String()
}

// isParameterPrefix checks whether position starts a $N or ?N placeholder.
//
// Takes sql (string) which is the SQL text to inspect.
// Takes position (int) which is the index to check.
//
// Returns bool which is true when the character at position is $ or ? followed
// by a digit 1-9.
func isParameterPrefix(sql string, position int) bool {
	if position+1 >= len(sql) {
		return false
	}
	prefix := sql[position]
	return (prefix == '$' || prefix == '?') && sql[position+1] >= '1' && sql[position+1] <= '9'
}

// renumberPlaceholder parses a $N or ?N placeholder, applies the exclusion
// offset, writes the renumbered placeholder, and returns the new position.
//
// Takes sql (string) which is the SQL text containing the placeholder.
// Takes position (int) which is the starting index of the placeholder prefix.
// Takes excluded (map[int]bool) which holds the excluded parameter numbers.
// Takes builder (*strings.Builder) which receives the renumbered placeholder.
//
// Returns int which is the position after the parsed placeholder.
func renumberPlaceholder(sql string, position int, excluded map[int]bool, builder *strings.Builder) int {
	prefix := sql[position]
	end := position + 1
	for end < len(sql) && sql[end] >= '0' && sql[end] <= '9' {
		end++
	}

	number := 0
	for _, digit := range sql[position+1 : end] {
		number = number*decimalRadix + int(digit-'0')
	}

	offset := 0
	for excludedNumber := range excluded {
		if excludedNumber < number {
			offset++
		}
	}

	fmt.Fprintf(builder, "%c%d", prefix, number-offset)
	return end
}
