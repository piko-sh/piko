package typegen_adapters

import (
	"context"
	"fmt"
	"strings"

	"piko.sh/piko/internal/annotator/annotator_dto"
)

const (
	// tsNewline is a newline character for TypeScript output formatting.
	tsNewline = "\n"

	// tsMethodClose is the TypeScript closing brace for a method definition.
	tsMethodClose = "    },\n"

	// tsJSDocOpen is the opening delimiter for a JSDoc comment block.
	tsJSDocOpen = "/**\n"

	// tsJSDocClose is the closing line for a TypeScript JSDoc comment block.
	tsJSDocClose = " */\n"

	// tsBlockCloseNL is a closing brace with trailing newlines for TypeScript blocks.
	tsBlockCloseNL = "}\n\n"

	// tsScenarioEndClose is the closing brace for a mock scenario object.
	tsScenarioEndClose = "      },\n"
)

// ActionMockEmitter creates TypeScript mock code for testing actions.
type ActionMockEmitter struct{}

// NewActionMockEmitter creates a new mock emitter.
//
// Returns *ActionMockEmitter which is ready for use in tests.
func NewActionMockEmitter() *ActionMockEmitter {
	return &ActionMockEmitter{}
}

// EmitMocks generates TypeScript mock code from action specs.
//
// Output is written to a separate file (actions.mock.ts) to keep the main
// actions file clean.
//
// Takes specs ([]annotator_dto.ActionSpec) which defines the actions to
// generate mocks for.
//
// Returns []byte which contains the generated TypeScript mock code.
// Returns error when code generation fails.
func (e *ActionMockEmitter) EmitMocks(_ context.Context, specs []annotator_dto.ActionSpec) ([]byte, error) {
	var b strings.Builder

	b.WriteString("// Auto-generated action mocks - DO NOT EDIT\n")
	b.WriteString("// Generated by piko action-generator\n")
	b.WriteString("// Use these mocks for testing action behaviour\n\n")

	e.emitImports(&b, specs)

	e.emitMockTypes(&b)
	e.emitCreateMockFunction(&b)

	e.emitMocksObject(&b, specs)

	e.emitTestHelpers(&b)

	return []byte(b.String()), nil
}

// emitImports writes the necessary imports from the main actions file.
//
// Takes b (*strings.Builder) which receives the generated import statements.
// Takes specs ([]annotator_dto.ActionSpec) which provides action specs to
// extract return types from.
func (*ActionMockEmitter) emitImports(b *strings.Builder, specs []annotator_dto.ActionSpec) {
	b.WriteString("import type { ActionError } from './action';\n")

	var responseTypes []string
	seen := make(map[string]bool)
	for i := range specs {
		if specs[i].ReturnType != nil && !seen[specs[i].ReturnType.Name] {
			seen[specs[i].ReturnType.Name] = true
			responseTypes = append(responseTypes, specs[i].ReturnType.Name)
		}
	}

	if len(responseTypes) > 0 {
		fmt.Fprintf(b, "import type { %s } from './actions';\n", strings.Join(responseTypes, ", "))
	}
	b.WriteString(tsNewline)
}

// emitMockTypes emits the MockConfig and ActionMock type definitions.
//
// Takes b (*strings.Builder) which receives the generated TypeScript code.
func (*ActionMockEmitter) emitMockTypes(b *strings.Builder) {
	b.WriteString(tsJSDocOpen)
	b.WriteString(" * Configuration for an action mock\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export interface MockConfig<T> {\n")
	b.WriteString("  /** Default response to return */\n")
	b.WriteString("  default: T;\n")
	b.WriteString("  /** Named scenarios that can be switched to */\n")
	b.WriteString("  scenarios?: Record<string, T | { error: ActionError }>;\n")
	b.WriteString(tsBlockCloseNL)

	b.WriteString(tsJSDocOpen)
	b.WriteString(" * A mock instance for an action\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export interface ActionMock<T> {\n")
	b.WriteString("  /** Set the response to a named scenario */\n")
	b.WriteString("  useScenario(name: string): void;\n")
	b.WriteString("  /** Reset to default response */\n")
	b.WriteString("  reset(): void;\n")
	b.WriteString("  /** Set a custom response */\n")
	b.WriteString("  setResponse(response: T | { error: ActionError }): void;\n")
	b.WriteString("  /** Get the current response (internal use) */\n")
	b.WriteString("  _getResponse(): T | { error: ActionError };\n")
	b.WriteString("  /** Record that the mock was called with args */\n")
	b.WriteString("  _recordCall(args: unknown[]): void;\n")
	b.WriteString("  /** Get all recorded calls */\n")
	b.WriteString("  getCalls(): unknown[][];\n")
	b.WriteString("  /** Clear recorded calls */\n")
	b.WriteString("  clearCalls(): void;\n")
	b.WriteString(tsBlockCloseNL)
}

// emitCreateMockFunction emits the createMock factory function.
//
// Takes b (*strings.Builder) which receives the generated TypeScript code.
func (*ActionMockEmitter) emitCreateMockFunction(b *strings.Builder) {
	b.WriteString(tsJSDocOpen)
	b.WriteString(" * Creates a mock for an action with configurable responses\n")
	b.WriteString(" *\n")
	b.WriteString(" * @example\n")
	b.WriteString(" * ```typescript\n")
	b.WriteString(" * const mock = createMock<UserResponse>({\n")
	b.WriteString(" *   default: { id: 1, name: 'Test' },\n")
	b.WriteString(" *   scenarios: {\n")
	b.WriteString(" *     notFound: { error: { status: 404, message: 'User not found' } },\n")
	b.WriteString(" *     serverError: { error: { status: 500, message: 'Server error' } },\n")
	b.WriteString(" *   },\n")
	b.WriteString(" * });\n")
	b.WriteString(" * ```\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export function createMock<T>(config: MockConfig<T>): ActionMock<T> {\n")
	b.WriteString("  let currentResponse: T | { error: ActionError } = config.default;\n")
	b.WriteString("  const calls: unknown[][] = [];\n")
	b.WriteString(tsNewline)
	b.WriteString("  return {\n")
	b.WriteString("    useScenario(name: string): void {\n")
	b.WriteString("      if (config.scenarios?.[name]) {\n")
	b.WriteString("        currentResponse = config.scenarios[name];\n")
	b.WriteString("      } else {\n")
	b.WriteString("        throw new Error(`Unknown scenario: ${name}`);\n")
	b.WriteString("      }\n")
	b.WriteString(tsMethodClose)
	b.WriteString(tsNewline)
	b.WriteString("    reset(): void {\n")
	b.WriteString("      currentResponse = config.default;\n")
	b.WriteString("      calls.length = 0;\n")
	b.WriteString(tsMethodClose)
	b.WriteString(tsNewline)
	b.WriteString("    setResponse(response: T | { error: ActionError }): void {\n")
	b.WriteString("      currentResponse = response;\n")
	b.WriteString(tsMethodClose)
	b.WriteString(tsNewline)
	b.WriteString("    _getResponse(): T | { error: ActionError } {\n")
	b.WriteString("      return currentResponse;\n")
	b.WriteString(tsMethodClose)
	b.WriteString(tsNewline)
	b.WriteString("    _recordCall(args: unknown[]): void {\n")
	b.WriteString("      calls.push(args);\n")
	b.WriteString(tsMethodClose)
	b.WriteString(tsNewline)
	b.WriteString("    getCalls(): unknown[][] {\n")
	b.WriteString("      return [...calls];\n")
	b.WriteString(tsMethodClose)
	b.WriteString(tsNewline)
	b.WriteString("    clearCalls(): void {\n")
	b.WriteString("      calls.length = 0;\n")
	b.WriteString(tsMethodClose)
	b.WriteString("  };\n")
	b.WriteString(tsBlockCloseNL)
}

// emitMocksObject emits the mocks object with entries for each action.
//
// Takes b (*strings.Builder) which receives the generated TypeScript code.
// Takes specs ([]annotator_dto.ActionSpec) which defines the actions to
// generate mocks for.
func (e *ActionMockEmitter) emitMocksObject(b *strings.Builder, specs []annotator_dto.ActionSpec) {
	b.WriteString(tsJSDocOpen)
	b.WriteString(" * Pre-configured mocks for all actions\n")
	b.WriteString(" *\n")
	b.WriteString(" * @example\n")
	b.WriteString(" * ```typescript\n")
	b.WriteString(" * // In test setup\n")
	b.WriteString(" * mocks.customerCreate.setResponse({ id: 123 });\n")
	b.WriteString(" *\n")
	b.WriteString(" * // Test error handling\n")
	b.WriteString(" * mocks.customerCreate.useScenario('validationError');\n")
	b.WriteString(" * ```\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export const mocks = {\n")

	for i := range specs {
		if i > 0 {
			b.WriteString(",\n")
		}

		returnType := "void"
		defaultValue := "undefined"
		if specs[i].ReturnType != nil {
			returnType = specs[i].ReturnType.Name
			defaultValue = e.generateDefaultValue(specs[i].ReturnType)
		}

		fmt.Fprintf(b, "  %s: createMock<%s>({\n", specs[i].TSFunctionName, returnType)
		fmt.Fprintf(b, "    default: %s,\n", defaultValue)
		b.WriteString("    scenarios: {\n")
		b.WriteString("      notFound: {\n")
		b.WriteString("        error: { status: 404, message: 'Not found', isNetworkError: false, isValidationError: false, isAuthError: false } as ActionError\n")
		b.WriteString(tsScenarioEndClose)
		b.WriteString("      validationError: {\n")
		b.WriteString("        error: { status: 422, message: 'Validation failed', validationErrors: {}, isNetworkError: false, isValidationError: true, isAuthError: false } as ActionError\n")
		b.WriteString(tsScenarioEndClose)
		b.WriteString("      serverError: {\n")
		b.WriteString("        error: { status: 500, message: 'Internal server error', isNetworkError: false, isValidationError: false, isAuthError: false } as ActionError\n")
		b.WriteString(tsScenarioEndClose)
		b.WriteString("      networkError: {\n")
		b.WriteString("        error: { status: 0, message: 'Network error', isNetworkError: true, isValidationError: false, isAuthError: false } as ActionError\n")
		b.WriteString(tsScenarioEndClose)
		b.WriteString("      unauthorized: {\n")
		b.WriteString("        error: { status: 401, message: 'Unauthorized', isNetworkError: false, isValidationError: false, isAuthError: false } as ActionError\n")
		b.WriteString(tsScenarioEndClose)
		b.WriteString(tsMethodClose)
		b.WriteString("  })")
	}

	b.WriteString("\n} as const;\n\n")
}

// generateDefaultValue generates a sensible default value for a type spec.
//
// Takes typeSpec (*annotator_dto.TypeSpec) which defines the type to
// generate a default value for.
//
// Returns string which contains the generated default value as a JavaScript
// object literal, or "undefined" if typeSpec is nil.
func (e *ActionMockEmitter) generateDefaultValue(typeSpec *annotator_dto.TypeSpec) string {
	if typeSpec == nil {
		return "undefined"
	}

	parts := make([]string, 0, len(typeSpec.Fields))
	for _, field := range typeSpec.Fields {
		defaultVal := e.defaultValueForTSType(field.TSType, field.Optional)
		parts = append(parts, fmt.Sprintf("%s: %s", field.JSONName, defaultVal))
	}

	if len(parts) == 0 {
		return "{}"
	}

	return fmt.Sprintf("{ %s }", strings.Join(parts, ", "))
}

// defaultValueForTSType returns a default value string for a TypeScript type.
//
// Takes tsType (string) which is the TypeScript type name to get a default for.
// Takes optional (bool) which indicates if the field is optional.
//
// Returns string which is the default value literal for the given type.
func (*ActionMockEmitter) defaultValueForTSType(tsType string, optional bool) string {
	if optional {
		return "undefined"
	}

	if strings.HasSuffix(tsType, "[]") {
		return "[]"
	}

	if strings.HasSuffix(tsType, " | null") {
		return "null"
	}

	switch tsType {
	case "string":
		return "''"
	case "number":
		return "0"
	case "boolean":
		return "false"
	case "Date":
		return "new Date()"
	case "File":
		return "new File([], 'test')"
	case "Blob":
		return "new Blob()"
	default:
		return "{} as " + tsType
	}
}

// emitTestHelpers emits utility functions for testing.
//
// Takes b (*strings.Builder) which receives the generated helper functions.
func (*ActionMockEmitter) emitTestHelpers(b *strings.Builder) {
	b.WriteString(tsJSDocOpen)
	b.WriteString(" * Resets all mocks to their default state\n")
	b.WriteString(" * Call this in beforeEach() or afterEach()\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export function resetAllMocks(): void {\n")
	b.WriteString("  for (const mock of Object.values(mocks)) {\n")
	b.WriteString("    mock.reset();\n")
	b.WriteString("  }\n")
	b.WriteString(tsBlockCloseNL)

	b.WriteString(tsJSDocOpen)
	b.WriteString(" * Gets all calls made to a specific mock\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export function getMockCalls<K extends keyof typeof mocks>(name: K): unknown[][] {\n")
	b.WriteString("  return mocks[name].getCalls();\n")
	b.WriteString(tsBlockCloseNL)

	b.WriteString(tsJSDocOpen)
	b.WriteString(" * Checks if a mock was called\n")
	b.WriteString(tsJSDocClose)
	b.WriteString("export function wasMockCalled<K extends keyof typeof mocks>(name: K): boolean {\n")
	b.WriteString("  return mocks[name].getCalls().length > 0;\n")
	b.WriteString("}\n")
}
