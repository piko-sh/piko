package layouter_domain

import "piko.sh/piko/internal/ast/ast_domain"

// BoxType classifies a LayoutBox for layout dispatch.
type BoxType int

const (
	// BoxBlock is a block-level box that participates in block formatting.
	BoxBlock BoxType = iota

	// BoxInline is an inline-level box that participates in inline
	// formatting.
	BoxInline

	// BoxInlineBlock is laid out as a block internally but placed as an
	// atomic inline unit.
	BoxInlineBlock

	// BoxFlex is a flex container.
	BoxFlex

	// BoxFlexItem is a direct child of a flex container.
	BoxFlexItem

	// BoxTable is a table formatting context.
	BoxTable

	// BoxTableRowGroup groups table rows (tbody, thead, tfoot).
	BoxTableRowGroup

	// BoxTableRow contains table cells.
	BoxTableRow

	// BoxTableCell is positioned within the table grid.
	BoxTableCell

	// BoxReplaced has intrinsic dimensions from an external resource (img,
	// svg, video).
	BoxReplaced

	// BoxTextRun is a run of text content with uniform styling.
	BoxTextRun

	// BoxAnonymousBlock wraps inline content inside a block container when
	// mixed with block children.
	BoxAnonymousBlock

	// BoxAnonymousInline wraps bare text content.
	BoxAnonymousInline

	// BoxListItem is a block-level box generated by display: list-item.
	BoxListItem

	// BoxListMarker is a marker box for list items, positioned
	// outside the content area for list-style-position: outside.
	BoxListMarker

	// BoxGrid is a grid container.
	BoxGrid

	// BoxGridItem is a direct child of a grid container.
	BoxGridItem

	// BoxNone represents a pruned subtree (display: none).
	BoxNone
)

// String returns the CSS-like name for the box type.
//
// Returns the human-readable box type name.
func (b BoxType) String() string {
	switch b {
	case BoxBlock:
		return "Block"
	case BoxInline:
		return "Inline"
	case BoxInlineBlock:
		return "InlineBlock"
	case BoxFlex:
		return "Flex"
	case BoxFlexItem:
		return "FlexItem"
	case BoxTable:
		return "Table"
	case BoxTableRowGroup:
		return "TableRowGroup"
	case BoxTableRow:
		return "TableRow"
	case BoxTableCell:
		return "TableCell"
	case BoxReplaced:
		return "Replaced"
	case BoxTextRun:
		return "TextRun"
	case BoxAnonymousBlock:
		return "AnonymousBlock"
	case BoxAnonymousInline:
		return "AnonymousInline"
	case BoxListItem:
		return "ListItem"
	case BoxListMarker:
		return "ListMarker"
	case BoxGrid:
		return "Grid"
	case BoxGridItem:
		return "GridItem"
	case BoxNone:
		return "None"
	default:
		return "Unknown"
	}
}

// IsInlineLevel reports whether this box type participates in
// inline formatting.
//
// Returns true for inline, inline-block, text run, and anonymous
// inline box types.
func (b BoxType) IsInlineLevel() bool {
	switch b {
	case BoxInline, BoxInlineBlock, BoxTextRun, BoxAnonymousInline:
		return true
	default:
		return false
	}
}

// IsBlockLevel reports whether this box type participates in
// block formatting.
//
// Returns true for block, flex, table, and related block-level
// box types.
func (b BoxType) IsBlockLevel() bool {
	switch b {
	case BoxBlock, BoxFlex, BoxTable, BoxAnonymousBlock, BoxFlexItem,
		BoxTableRowGroup, BoxTableRow, BoxTableCell, BoxListItem,
		BoxGrid, BoxGridItem:
		return true
	default:
		return false
	}
}

// BoxEdges holds four-sided edge values in points, used for padding, border
// widths, and margins.
type BoxEdges struct {
	// Top is the top edge value in points.
	Top float64

	// Right is the right edge value in points.
	Right float64

	// Bottom is the bottom edge value in points.
	Bottom float64

	// Left is the left edge value in points.
	Left float64
}

// Horizontal returns the sum of the left and right edges.
//
// Returns the combined horizontal edge width in points.
func (e BoxEdges) Horizontal() float64 {
	return e.Left + e.Right
}

// Vertical returns the sum of the top and bottom edges.
//
// Returns the combined vertical edge height in points.
func (e BoxEdges) Vertical() float64 {
	return e.Top + e.Bottom
}

// LayoutBox is the fundamental node in the layout box tree. Each LayoutBox
// represents a CSS box with resolved styles, tree relationships, and layout
// results.
type LayoutBox struct {
	// SourceNode is a back-reference to the originating TemplateNode. Nil
	// for anonymous boxes.
	SourceNode *ast_domain.TemplateNode

	// Parent is the parent LayoutBox in the tree. Nil for the root box.
	Parent *LayoutBox

	// ContainingBlock is the nearest positioned or transformed
	// ancestor for absolutely-positioned descendants. Set
	// during box tree construction.
	ContainingBlock *LayoutBox

	// TransformAncestor is the nearest ancestor with a CSS
	// transform other than "none", or nil when no transformed
	// ancestor exists.
	TransformAncestor *LayoutBox

	// Children are the child boxes in document order.
	Children []*LayoutBox

	// Text is the string content for TextRun boxes.
	Text string

	// Glyphs holds the shaped glyph positions for TextRun
	// boxes, or nil for non-text boxes.
	Glyphs []GlyphPosition

	// Style is the fully resolved computed style for this box.
	Style ComputedStyle

	// Padding holds the resolved padding values in points.
	Padding BoxEdges

	// Border holds the resolved border widths in points.
	Border BoxEdges

	// Margin holds the resolved margin values in points.
	Margin BoxEdges

	// ContentX is the X coordinate of the content area's top-left corner
	// in document coordinates (points).
	ContentX float64

	// ContentY is the Y coordinate of the content area's top-left corner.
	ContentY float64

	// ContentWidth is the width of the content area in points.
	ContentWidth float64

	// ContentHeight is the height of the content area in points.
	ContentHeight float64

	// IntrinsicWidth is the natural width of a replaced element (e.g.
	// image) in points.
	IntrinsicWidth float64

	// IntrinsicHeight is the natural height of a replaced element in
	// points.
	IntrinsicHeight float64

	// Type classifies this box for layout dispatch.
	Type BoxType

	// PageIndex identifies which page this box appears on (zero-based).
	PageIndex int

	// PageYOffset is the cumulative Y displacement from forced page breaks
	// (break-before, break-after). Used by position extraction to compute
	// correct page-relative Y coordinates.
	PageYOffset float64

	// Colspan is the number of columns this table cell spans.
	// Defaults to 1 for non-spanning cells.
	Colspan int

	// Rowspan is the number of rows this table cell spans.
	// Defaults to 1 for non-spanning cells.
	Rowspan int

	// OffsetX is the horizontal offset applied by
	// position: relative, computed during layout and
	// applied in a final pass.
	OffsetX float64

	// OffsetY is the vertical offset applied by
	// position: relative, computed during layout and
	// applied in a final pass.
	OffsetY float64

	// BaselineOffset is the distance from the top of the text
	// content box to the text baseline, computed from font metrics
	// and half-leading during inline layout.
	BaselineOffset float64

	// IsListMarker is true for text runs that represent a
	// list marker generated for list-style-position: inside.
	IsListMarker bool
}

// BorderBoxWidth returns the total width including padding,
// borders, and content.
//
// Returns the border box width in points.
func (b *LayoutBox) BorderBoxWidth() float64 {
	return b.ContentWidth + b.Padding.Horizontal() + b.Border.Horizontal()
}

// BorderBoxHeight returns the total height including padding,
// borders, and content.
//
// Returns the border box height in points.
func (b *LayoutBox) BorderBoxHeight() float64 {
	return b.ContentHeight + b.Padding.Vertical() + b.Border.Vertical()
}

// BorderBoxX returns the X coordinate of the border box's
// top-left corner.
//
// Returns the X position in document coordinates (points).
func (b *LayoutBox) BorderBoxX() float64 {
	return b.ContentX - b.Padding.Left - b.Border.Left
}

// BorderBoxY returns the Y coordinate of the border box's
// top-left corner.
//
// Returns the Y position in document coordinates (points).
func (b *LayoutBox) BorderBoxY() float64 {
	return b.ContentY - b.Padding.Top - b.Border.Top
}

// MarginBoxWidth returns the total width including margins,
// padding, borders, and content.
//
// Returns the margin box width in points.
func (b *LayoutBox) MarginBoxWidth() float64 {
	return b.BorderBoxWidth() + b.Margin.Horizontal()
}

// MarginBoxHeight returns the total height including margins,
// padding, borders, and content.
//
// Returns the margin box height in points.
func (b *LayoutBox) MarginBoxHeight() float64 {
	return b.BorderBoxHeight() + b.Margin.Vertical()
}

// TagName returns the HTML tag name of the source node, or a
// descriptive string for anonymous and text boxes.
//
// Returns the tag name or a placeholder like "#text".
func (b *LayoutBox) TagName() string {
	if b.SourceNode != nil && b.SourceNode.TagName != "" {
		return b.SourceNode.TagName
	}
	switch b.Type {
	case BoxTextRun:
		return "#text"
	case BoxAnonymousBlock:
		return "#anonymous-block"
	case BoxAnonymousInline:
		return "#anonymous-inline"
	default:
		return "#root"
	}
}
