// generateSingleArtefact contains the core logic for generating one Go file
// from a component. It orchestrates the code emitter and packages the output
// into a self-contained GeneratedArtefact.
//
// Takes request (generator_dto.GenerateRequest) which specifies the generation
// settings and source path.
// Takes annotationResult (*annotator_dto.AnnotationResult) which provides the
// parsed annotations from the source file.
// Takes vc (*annotator_dto.VirtualComponent) which defines the component to
// generate code for.
//
// Returns *generator_dto.GeneratedArtefact which contains the generated code
// and metadata.
// Returns error when code emission fails or produces diagnostics.
func (s *generatorService) generateSingleArtefact(
	ctx context.Context,
	request generator_dto.GenerateRequest,
	annotationResult *annotator_dto.AnnotationResult,
	vc *annotator_dto.VirtualComponent,
) (*generator_dto.GeneratedArtefact, error) {
	codeEmitter := s.codeEmitterFactory.NewEmitter()
	request.CanonicalGoPackagePath = vc.CanonicalGoPackagePath

	emittedCode, emitDiags, err := codeEmitter.EmitCode(ctx, annotationResult, request)
	if err != nil {
		return nil, fmt.Errorf("fatal error emitting code for %s: %w", request.SourcePath, err)
	}
	if len(emitDiags) > 0 {
		return nil, annotator_domain.NewSemanticError(emitDiags)
	}

	logCodeEmissionDebug(ctx, request.SourcePath, emittedCode)

	jsPath := s.emitClientSideJS(ctx, request, annotationResult)

	return &generator_dto.GeneratedArtefact{
		Content:       emittedCode,
		SuggestedPath: request.OutputPath,
		Result:        annotationResult,
		Component:     vc,
		JSArtefactID:  jsPath,
	}, nil
}

// emitClientSideJS emits client-side JavaScript if configured and present.
//
// Takes request (generator_dto.GenerateRequest) which specifies the source path
// and base directory for the page.
// Takes annotationResult (*annotator_dto.AnnotationResult) which contains the
// client script to emit.
//
// Returns string which is the path to the emitted JS file, or empty string if
// not emitted.
func (s *generatorService) emitClientSideJS(
	ctx context.Context,
	request generator_dto.GenerateRequest,
	annotationResult *annotator_dto.AnnotationResult,
) string {
	if s.pkJSEmitter == nil || annotationResult.ClientScript == "" {
		return ""
	}

	ctx, l := logger_domain.From(ctx, log)
	pagePath := derivePagePath(request.SourcePath, request.BaseDir)
	outputDir := filepath.Join(s.baseDir, distDir)

	jsPath, err := s.pkJSEmitter.EmitJS(ctx, annotationResult.ClientScript, pagePath, request.ModuleName, outputDir, false)
	if err != nil {
		l.Warn("Failed to emit client-side JS, continuing without it",
			logger_domain.String("source", request.SourcePath),
			logger_domain.Error(err))
		return ""
	}

	l.Trace("Emitted client-side JS",
		logger_domain.String("source", request.SourcePath),
		logger_domain.String("jsPath", jsPath))
	return jsPath
}

// cleanOrphanedDirs removes generated directories that no longer have
// matching source files. It scans the pages, partials, and emails output
// directories and removes any subdirectories that are not present in the
// current VirtualModule's ComponentsByHash map.
//
// Takes virtualModule (*annotator_dto.VirtualModule) which contains the current
// set of valid components.
func (s *generatorService) cleanOrphanedDirs(
	ctx context.Context,
	virtualModule *annotator_dto.VirtualModule,
) {
	validHashes := make(map[string]bool)
	for hashedName := range virtualModule.ComponentsByHash {
		validHashes[hashedName] = true
	}

	targetDirs := []string{
		config.CompiledPagesTargetDir,
		config.CompiledPartialsTargetDir,
		config.CompiledEmailsTargetDir,
	}

	for _, targetDir := range targetDirs {
		if targetDir == "" {
			continue
		}
		s.cleanOrphanedInDir(ctx, targetDir, validHashes)
	}
}

// cleanOrphanedInDir removes orphaned subdirectories from a single target
// directory.
//
// Takes directory (string) which is the directory to scan for orphaned
// subdirectories.
// Takes validHashes (map[string]bool) which is the set of valid component
// hashes.
func (s *generatorService) cleanOrphanedInDir(
	ctx context.Context,
	directory string,
	validHashes map[string]bool,
) {
	ctx, l := logger_domain.From(ctx, log)
	entries, err := s.fsWriter.ReadDir(directory)
	if err != nil {
		return
	}

	for _, entry := range entries {
		if ctx.Err() != nil {
			return
		}

		if !entry.IsDir() {
			continue
		}
		dirName := entry.Name()
		if !validHashes[dirName] {
			fullPath := filepath.Join(directory, dirName)
			l.Internal("Removing orphaned generated directory", logger_domain.String("path", fullPath))
			if err := s.fsWriter.RemoveAll(fullPath); err != nil {
				l.Warn("Failed to remove orphaned directory",
					logger_domain.String("path", fullPath),
					logger_domain.Error(err))
			}
		}
	}
}

