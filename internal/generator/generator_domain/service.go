// GenerateProject orchestrates the full compilation pipeline for an entire
// project. It annotates all entry points and their dependencies, generates Go
// code for every discovered component (pages and partials), and finally
// produces a project manifest and a registration file.
//
// Actions are auto-discovered from the actions/ directory during annotation.
//
// Takes entryPoints ([]annotator_dto.EntryPoint) which lists the pages
// and layouts to compile.
//
// Returns artefacts ([]*generator_dto.GeneratedArtefact) which contains
// the generated Go source files.
// Returns manifest (*generator_dto.Manifest) which describes the
// project's pages, partials, and routes.
// Returns err (error) when any phase of the pipeline fails.
func (s *generatorService) GenerateProject(
	ctx context.Context,
	entryPoints []annotator_dto.EntryPoint,
) (artefacts []*generator_dto.GeneratedArtefact, manifest *generator_dto.Manifest, err error) {
	ctx, span, _ := log.Span(ctx, "GeneratorService.GenerateProject")
	defer span.End()

	generateProjectCount.Add(ctx, 1)
	startTime := time.Now()
	defer func() {
		generateProjectDuration.Record(ctx, float64(time.Since(startTime).Milliseconds()))
		if err != nil {
			generateProjectErrorCount.Add(ctx, 1)
		}
	}()

	projectResult, err := s.runAnnotationPhase(ctx, entryPoints)
	if err != nil {
		return nil, nil, fmt.Errorf("running annotation phase: %w", err)
	}

	return s.EmitProject(ctx, projectResult)
}

// EmitProject runs the post-annotation emission pipeline: static collections,
// search indexes, i18n, actions, parallel code generation, manifest building,
// and SEO artefacts. It takes a pre-computed annotation result so the caller
// can run annotation once and fan out emission and asset building in parallel.
//
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which contains
// the annotated project data from AnnotateProject.
//
// Returns []*generator_dto.GeneratedArtefact which contains all generated Go
// source files.
// Returns *generator_dto.Manifest which describes the project's pages,
// partials, and routes.
// Returns error when any emission step fails.
func (s *generatorService) EmitProject(
	ctx context.Context,
	projectResult *annotator_dto.ProjectAnnotationResult,
) ([]*generator_dto.GeneratedArtefact, *generator_dto.Manifest, error) {
	ctx, span, l := log.Span(ctx, "GeneratorService.EmitProject")
	defer span.End()

	if len(projectResult.ComponentResults) == 0 {
		l.Internal("No components found in the project, nothing to generate.")
		return []*generator_dto.GeneratedArtefact{}, &generator_dto.Manifest{}, nil
	}

	collectionPackagePaths, collectionItems, err := s.generateStaticCollections(ctx, projectResult)
	if err != nil {
		return nil, nil, fmt.Errorf("generating static collections: %w", err)
	}

	if err := s.generateSearchIndexes(ctx, collectionItems); err != nil {
		return nil, nil, fmt.Errorf("generating search indexes: %w", err)
	}

	if err := s.generateI18nFlatBuffer(ctx); err != nil {
		return nil, nil, fmt.Errorf("generating i18n FlatBuffer: %w", err)
	}

	actionsPackagePath, err := s.generateActionFiles(ctx, projectResult)
	if err != nil {
		return nil, nil, fmt.Errorf("generating action files: %w", err)
	}

	artefactMap, err := s.generateComponentsInParallel(ctx, projectResult)
	if err != nil {
		return nil, nil, fmt.Errorf("generating components in parallel: %w", err)
	}

	allArtefacts, allPackagePaths, err := s.aggregateArtefactsAndDiagnostics(ctx, artefactMap, projectResult)
	if err != nil {
		return nil, nil, fmt.Errorf("aggregating artefacts and diagnostics: %w", err)
	}

	return s.finaliseProjectGeneration(ctx, projectResult, allArtefacts, allPackagePaths, collectionPackagePaths, actionsPackagePath)
}

// AnnotateProject runs only the annotation phase of the compilation pipeline,
// returning the project analysis result without generating any code. This is
// used by assets-only builds where the FinalAssetManifest is needed but code
// emission and formatting are not.
//
// Takes entryPoints ([]annotator_dto.EntryPoint) which contains the source
// files to analyse.
//
// Returns *annotator_dto.ProjectAnnotationResult which contains the
// FinalAssetManifest and all component metadata.
// Returns error when annotation fails.
func (s *generatorService) AnnotateProject(
	ctx context.Context,
	entryPoints []annotator_dto.EntryPoint,
) (*annotator_dto.ProjectAnnotationResult, error) {
	return s.runAnnotationPhase(ctx, entryPoints)
}

// finaliseProjectGeneration completes project generation by building the
// manifest and running cleanup tasks.
//
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which contains
// the annotated project data.
// Takes allArtefacts ([]*generator_dto.GeneratedArtefact) which holds the
// generated artefacts to include in the manifest.
// Takes allPackagePaths ([]string) which lists the package paths to register.
// Takes collectionPackagePaths ([]string) which lists additional collection
// package paths to append.
// Takes actionsPackagePath (string) which specifies the actions package path, or
// empty if none.
//
// Returns []*generator_dto.GeneratedArtefact which contains all registered
// artefacts.
// Returns *generator_dto.Manifest which is the built project manifest.
// Returns error when manifest building fails.
func (s *generatorService) finaliseProjectGeneration(
	ctx context.Context,
	projectResult *annotator_dto.ProjectAnnotationResult,
	allArtefacts []*generator_dto.GeneratedArtefact,
	allPackagePaths, collectionPackagePaths []string,
	actionsPackagePath string,
) ([]*generator_dto.GeneratedArtefact, *generator_dto.Manifest, error) {
	ctx, l := logger_domain.From(ctx, log)
	allPackagePaths = append(allPackagePaths, collectionPackagePaths...)
	if actionsPackagePath != "" {
		allPackagePaths = append(allPackagePaths, actionsPackagePath)
	}

	manifest, allArtefacts, err := s.buildManifestAndRegister(ctx, allArtefacts, allPackagePaths)
	if err != nil {
		return nil, nil, fmt.Errorf("building manifest and register file: %w", err)
	}

	s.generateSEOArtefacts(ctx, projectResult)
	s.cleanOrphanedDirs(ctx, projectResult.VirtualModule)
	l.Internal("Project generation finished successfully.")
	return allArtefacts, manifest, nil
}

// runSingleFileAnnotation runs the annotation process for a single file.
//
// Takes request (generator_dto.GenerateRequest) which specifies the file to
// annotate.
//
// Returns *annotator_dto.ProjectAnnotationResult which contains the annotation
// output for the file.
// Returns error when the annotation process fails.
func (s *generatorService) runSingleFileAnnotation(
	ctx context.Context,
	request generator_dto.GenerateRequest,
) (*annotator_dto.ProjectAnnotationResult, error) {
	entryPoints := []annotator_dto.EntryPoint{{
		Path:              request.SourcePath,
		IsPage:            request.IsPage,
		IsPublic:          false,
		IsEmail:           false,
		VirtualPageSource: nil,
	}}
	return s.coordinator.GetResult(ctx, entryPoints, coordinator_domain.WithCausationID(generatorCausationID))
}

// resolveTargetComponent finds the target component from the annotation result.
//
// Takes request (generator_dto.GenerateRequest) which specifies the source path to
// find.
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which contains
// the annotated project data.
//
// Returns *annotator_dto.AnnotationResult which is the annotation result for
// the target component.
// Returns *annotator_dto.VirtualComponent which is the main component at the
// entry point.
// Returns error when the path cannot be found, the entry point is not in the
// graph, or compilation failed with errors.
func (s *generatorService) resolveTargetComponent(
	ctx context.Context,
	request generator_dto.GenerateRequest,
	projectResult *annotator_dto.ProjectAnnotationResult,
) (*annotator_dto.AnnotationResult, *annotator_dto.VirtualComponent, error) {
	absoluteSourcePath, err := s.resolver.ResolvePKPath(ctx, request.SourcePath, "")
	if err != nil {
		return nil, nil, fmt.Errorf("could not resolve entry point path '%s': %w", request.SourcePath, err)
	}

	hashedName, ok := projectResult.VirtualModule.Graph.PathToHashedName[absoluteSourcePath]
	if !ok {
		return nil, nil, fmt.Errorf("internal error: no hash found for entry point %s", request.SourcePath)
	}

	targetResult, ok := projectResult.ComponentResults[hashedName]
	if !ok {
		if ast_domain.HasErrors(projectResult.AllDiagnostics) {
			formattedErrors := annotator_domain.FormatAllDiagnostics(projectResult.AllDiagnostics, projectResult.AllSourceContents)
			return nil, nil, fmt.Errorf("compilation failed with errors:\n%s", formattedErrors)
		}
		return nil, nil, fmt.Errorf("internal error: no annotation result found for entry point %s", request.SourcePath)
	}

	mainComponent := targetResult.VirtualModule.ComponentsByHash[hashedName]
	return targetResult, mainComponent, nil
}

// singleFileGenerationContext holds all the context needed for generating a
// single file.
type singleFileGenerationContext struct {
	// targetResult holds the annotation result for the file being processed.
	targetResult *annotator_dto.AnnotationResult

	// mainComponent is the component being documented in this file.
	mainComponent *annotator_dto.VirtualComponent

	// projectResult holds the project-wide annotation data, including all
	// diagnostics.
	projectResult *annotator_dto.ProjectAnnotationResult

	// request holds the parameters for the generation request.
	request generator_dto.GenerateRequest
}

// generateAndFinaliseArtefact creates the artefact and adds diagnostics to it.
//
// Takes genCtx (*singleFileGenerationContext) which holds the generation
// context, including the request and target.
//
// Returns *generator_dto.GeneratedArtefact which holds the created artefact
// with project-wide diagnostics added.
// Returns error when creation fails or the code has errors.
func (s *generatorService) generateAndFinaliseArtefact(ctx context.Context, genCtx *singleFileGenerationContext) (*generator_dto.GeneratedArtefact, error) {
	artefact, err := s.generateSingleArtefact(ctx, genCtx.request, genCtx.targetResult, genCtx.mainComponent)
	if err != nil {
		if semanticErr, ok := errors.AsType[*annotator_domain.SemanticError](err); ok {
			formattedErrors := annotator_domain.FormatAllDiagnostics(semanticErr.Diagnostics, genCtx.projectResult.AllSourceContents)
			return nil, fmt.Errorf("compilation failed with errors:\n%s", formattedErrors)
		}
		return nil, fmt.Errorf("generating single artefact: %w", err)
	}

	artefact.Result.AnnotatedAST.Diagnostics = genCtx.projectResult.AllDiagnostics
	return artefact, nil
}

// runAnnotationPhase runs dependency analysis and annotation for all parts.
//
// Actions are found from the actions/ folder during annotation.
//
// Takes entryPoints ([]annotator_dto.EntryPoint) which specifies the packages
// to analyse.
//
// Returns *annotator_dto.ProjectAnnotationResult which contains the full
// annotation results for all entry points.
// Returns error when compilation fails or a fatal error occurs during analysis.
func (s *generatorService) runAnnotationPhase(
	ctx context.Context,
	entryPoints []annotator_dto.EntryPoint,
) (*annotator_dto.ProjectAnnotationResult, error) {
	ctx, l := logger_domain.From(ctx, log)
	l.Internal("Running annotation phase for all entry points...")
	projectResult, err := s.coordinator.GetResult(
		ctx,
		entryPoints,
		coordinator_domain.WithCausationID(generatorCausationID),
	)
	if err != nil {
		if semanticErr, ok := errors.AsType[*annotator_domain.SemanticError](err); ok && projectResult != nil {
			formattedErrors := annotator_domain.FormatAllDiagnostics(semanticErr.Diagnostics, projectResult.AllSourceContents)
			return nil, fmt.Errorf("compilation failed during analysis:\n%s", formattedErrors)
		}
		return nil, fmt.Errorf("build failed with a fatal error during analysis: %w", err)
	}
	l.Internal("Annotation phase successful.")
	return projectResult, nil
}

// generateStaticCollections creates binary collection files for all static
// collections.
//
// This method gets collection data from the VirtualModule (created during
// annotation), groups items by collection, and writes binary files to
// dist/collections/{collectionName}/.
//
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which contains
// the virtual module with collection data.
//
// Returns []string which contains Go package paths for the generated
// collection packages.
// Returns map[string][]collection_dto.ContentItem which maps collection names
// to their items for search index generation.
// Returns error when any collection fails to generate.
func (s *generatorService) generateStaticCollections(
	ctx context.Context,
	projectResult *annotator_dto.ProjectAnnotationResult,
) ([]string, map[string][]collection_dto.ContentItem, error) {
	ctx, l := logger_domain.From(ctx, log)
	if s.collectionEmitter == nil {
		l.Internal("No collection emitter configured, skipping collection generation.")
		return nil, nil, nil
	}

	l.Internal("Starting static collection generation...")

	collectionItems := extractCollectionItemsFromModule(projectResult.VirtualModule)
	if len(collectionItems) == 0 {
		l.Internal("No static collections found in virtual module.")
		return nil, nil, nil
	}

	l.Internal("Found static collections to generate", logger_domain.Int("collection_count", len(collectionItems)))

	packagePaths, err := s.emitCollections(ctx, collectionItems)
	if err != nil {
		return nil, nil, fmt.Errorf("emitting static collections: %w", err)
	}

	l.Internal("Static collection generation complete", logger_domain.Int("collections_generated", len(packagePaths)))
	return packagePaths, collectionItems, nil
}

// emitCollections builds binary output for all collection items.
//
// Takes collectionItems (map[string][]collection_dto.ContentItem) which maps
// collection names to their content items.
//
// Returns []string which contains the package paths for each generated
// collection.
// Returns error when a collection fails to build.
func (s *generatorService) emitCollections(
	ctx context.Context,
	collectionItems map[string][]collection_dto.ContentItem,
) ([]string, error) {
	ctx, l := logger_domain.From(ctx, log)
	packagePaths := make([]string, 0, len(collectionItems))
	outputDir := filepath.Join(s.baseDir, distDir)

	for collectionName, items := range collectionItems {
		if ctx.Err() != nil {
			return nil, ctx.Err()
		}

		l.Trace("Generating collection",
			logger_domain.String(logKeyCollection, collectionName),
			logger_domain.Int("item_count", len(items)))

		packagePath, err := s.collectionEmitter.EmitCollection(ctx, collectionName, items, outputDir)
		if err != nil {
			return nil, fmt.Errorf("failed to generate collection %q: %w", collectionName, err)
		}

		packagePaths = append(packagePaths, packagePath)
		l.Trace("Collection generated successfully",
			logger_domain.String(logKeyCollection, collectionName),
			logger_domain.String("package_path", packagePath))
	}

	return packagePaths, nil
}

// generateSearchIndexes generates search index binaries for all static
// collections.
//
// This method builds inverted indexes for Fast and Smart modes, enabling
// zero-copy full-text search with BM25 ranking at runtime.
//
// Takes collectionItems (map[string][]collection_dto.ContentItem) which maps
// collection names to their content items.
//
// Returns error when any index fails to generate.
func (s *generatorService) generateSearchIndexes(
	ctx context.Context,
	collectionItems map[string][]collection_dto.ContentItem,
) error {
	ctx, l := logger_domain.From(ctx, log)
	if s.searchIndexEmitter == nil {
		l.Internal("No search index emitter configured, skipping search index generation.")
		return nil
	}

	if len(collectionItems) == 0 {
		l.Internal("No collections to index for search.")
		return nil
	}

	l.Internal("Starting search index generation...", logger_domain.Int("collection_count", len(collectionItems)))

	outputDir := filepath.Join(s.baseDir, distDir)

	for collectionName, items := range collectionItems {
		if ctx.Err() != nil {
			return ctx.Err()
		}

		l.Trace("Generating search indexes for collection",
			logger_domain.String(logKeyCollection, collectionName),
			logger_domain.Int("item_count", len(items)))

		modes := []string{"fast", "smart"}

		err := s.searchIndexEmitter.EmitSearchIndex(ctx, collectionName, items, outputDir, modes)
		if err != nil {
			return fmt.Errorf("failed to generate search indexes for collection %q: %w", collectionName, err)
		}

		l.Trace("Search indexes generated successfully",
			logger_domain.String(logKeyCollection, collectionName),
			logger_domain.Strings("modes", modes))
	}

	l.Internal("Search index generation complete", logger_domain.Int("collections_indexed", len(collectionItems)))
	return nil
}

// generateI18nFlatBuffer creates the i18n FlatBuffer binary file if an emitter
// is set up.
//
// Returns error when the FlatBuffer creation fails.
func (s *generatorService) generateI18nFlatBuffer(
	ctx context.Context,
) error {
	ctx, l := logger_domain.From(ctx, log)
	if s.i18nEmitter == nil {
		l.Internal("No i18n emitter configured, skipping i18n FlatBuffer generation.")
		return nil
	}

	l.Internal("Starting i18n FlatBuffer generation...")

	outputPath := filepath.Join(s.baseDir, distDir, "i18n.bin")

	if err := s.i18nEmitter.EmitI18n(ctx, outputPath); err != nil {
		return fmt.Errorf("failed to generate i18n FlatBuffer: %w", err)
	}

	l.Internal("i18n FlatBuffer generation complete", logger_domain.String("output_path", outputPath))
	return nil
}

// generateActionFiles generates action code files from the discovered
// ActionManifest if an action generator is configured.
//
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which contains
// the discovered action manifest and virtual module data.
//
// Returns string which is the package path of the generated actions package,
// or empty if no generator is configured.
// Returns error when action code generation fails.
func (s *generatorService) generateActionFiles(
	ctx context.Context,
	projectResult *annotator_dto.ProjectAnnotationResult,
) (string, error) {
	ctx, l := logger_domain.From(ctx, log)
	if s.actionGenerator == nil {
		l.Internal("No action generator configured, skipping action file generation.")
		return "", nil
	}

	manifest := projectResult.VirtualModule.ActionManifest
	outputDir := s.baseDir
	actionsGenTSPath := filepath.Join(outputDir, "dist", "ts", "actions.gen.ts")

	actionsPackagePath, err := s.generateActionSource(ctx, manifest, outputDir, actionsGenTSPath)
	if err != nil {
		return "", fmt.Errorf("generating action source: %w", err)
	}

	if err := s.emitActionsJS(ctx, actionsGenTSPath, outputDir); err != nil {
		return "", fmt.Errorf("emitting actions JavaScript: %w", err)
	}

	actionCount := 0
	if manifest != nil {
		actionCount = len(manifest.Actions)
	}
	l.Internal("Action file generation complete",
		logger_domain.Int("action_count", actionCount),
		logger_domain.String("package_path", actionsPackagePath))
	return actionsPackagePath, nil
}

// generateActionSource generates the TypeScript action source file.
//
// Takes manifest (*annotator_dto.ActionManifest) which contains the discovered
// actions to generate.
// Takes outputDir (string) which specifies the output directory path.
// Takes actionsGenTSPath (string) which specifies the path for the generated
// TypeScript file.
//
// Returns string which is the module path for the generated actions, or empty
// if no actions were found.
// Returns error when directory creation or file writing fails.
func (s *generatorService) generateActionSource(
	ctx context.Context,
	manifest *annotator_dto.ActionManifest,
	outputDir, actionsGenTSPath string,
) (string, error) {
	ctx, l := logger_domain.From(ctx, log)
	if manifest == nil || len(manifest.Actions) == 0 {
		l.Internal("No actions discovered, generating empty actions stub for components.")
		if s.baseSandbox != nil {
			relPath, _ := filepath.Rel(s.baseDir, actionsGenTSPath)
			if err := s.baseSandbox.MkdirAll(filepath.Dir(relPath), PublicDirPermission); err != nil {
				return "", fmt.Errorf("creating pk directory: %w", err)
			}
			if err := s.baseSandbox.WriteFile(relPath, []byte(emptyActionsStub), PublicFilePermission); err != nil {
				return "", fmt.Errorf("writing empty actions stub: %w", err)
			}
		}
		return "", nil
	}

	l.Internal("Starting action file generation...",
		logger_domain.Int("action_count", len(manifest.Actions)))

	moduleName := s.resolver.GetModuleName()
	if err := s.actionGenerator.GenerateActions(ctx, manifest, moduleName, outputDir); err != nil {
		return "", fmt.Errorf("failed to generate action files: %w", err)
	}
	return paths.Join(moduleName, distDir, "actions"), nil
}

// emitActionsJS transpiles the TypeScript actions file to JavaScript.
//
// Takes actionsGenTSPath (string) which is the path to the TypeScript file.
// Takes outputDir (string) which is the directory for the emitted JavaScript.
//
// Returns error when the JavaScript emission fails.
func (s *generatorService) emitActionsJS(
	ctx context.Context,
	actionsGenTSPath, outputDir string,
) error {
	ctx, l := logger_domain.From(ctx, log)
	if s.baseSandbox == nil {
		return nil
	}
	relPath, _ := filepath.Rel(s.baseDir, actionsGenTSPath)
	tsSource, err := s.baseSandbox.ReadFile(relPath)
	if err != nil || len(tsSource) == 0 {
		return nil
	}

	if _, err := s.pkJSEmitter.EmitJS(ctx, string(tsSource), "pk/actions.gen", s.resolver.GetModuleName(), outputDir, false); err != nil {
		return fmt.Errorf("failed to emit actions JavaScript: %w", err)
	}
	l.Internal("Emitted actions JavaScript for browser consumption")
	return nil
}

// workerContext holds all the shared state needed by worker goroutines during
// parallel generation.
type workerContext struct {
	// projectResult holds the pre-computed annotation results for the project.
	projectResult *annotator_dto.ProjectAnnotationResult

	// artefactMap stores generated artefacts or errors, keyed by source path.
	artefactMap *syncmap.Map
}

// generateComponentsInParallel sets up a worker pool to generate Go code for
// all components at the same time.
//
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which provides
// the annotated project with all components to generate.
//
// Returns *syncmap.Map which contains the generated results keyed by
// component hash.
// Returns error when any worker fails during code generation.
func (s *generatorService) generateComponentsInParallel(
	ctx context.Context,
	projectResult *annotator_dto.ProjectAnnotationResult,
) (*syncmap.Map, error) {
	ctx, l := logger_domain.From(ctx, log)

	virtualModule := projectResult.VirtualModule
	allComponentsToGenerate := virtualModule.ComponentsByHash

	l.Internal("Starting project generation pipeline...", logger_domain.Int("component_count", len(allComponentsToGenerate)))

	var artefactMap syncmap.Map
	g, gCtx := errgroup.WithContext(ctx)
	jobs := make(chan *annotator_dto.VirtualComponent, len(allComponentsToGenerate))

	wctx := &workerContext{
		projectResult: projectResult,
		artefactMap:   &artefactMap,
	}

	numWorkers := calculateWorkerCount(len(allComponentsToGenerate))
	for range numWorkers {
		g.Go(func() error {
			return s.processComponentWorker(gCtx, jobs, wctx)
		})
	}

	for _, vc := range allComponentsToGenerate {
		jobs <- vc
	}
	close(jobs)

	if err := g.Wait(); err != nil {
		return nil, fmt.Errorf("processing components in parallel: %w", err)
	}

	return &artefactMap, nil
}

// processComponentWorker reads components from the jobs channel and generates
// code for each one.
//
// Takes jobs (<-chan *annotator_dto.VirtualComponent) which provides
// components to process.
// Takes wctx (*workerContext) which holds shared state for the worker.
//
// Returns error when processing fails.
func (s *generatorService) processComponentWorker(
	ctx context.Context,
	jobs <-chan *annotator_dto.VirtualComponent,
	wctx *workerContext,
) error {
	ctx, l := logger_domain.From(ctx, log)

	for vc := range jobs {
		if ctx.Err() != nil {
			break
		}

		l.Trace("Worker processing component...", logger_domain.String("sourcePath", vc.Source.SourcePath))

		annotationResult, ok := wctx.projectResult.ComponentResults[vc.HashedName]
		if !ok {
			err := fmt.Errorf("internal consistency error: no annotation result found for component hash %s", vc.HashedName)
			wctx.artefactMap.Store(vc.Source.SourcePath, err)
			continue
		}

		request := generator_dto.GenerateRequest{
			SourcePath:                vc.Source.SourcePath,
			OutputPath:                vc.VirtualGoFilePath,
			PackageName:               vc.HashedName,
			BaseDir:                   s.baseDir,
			IsPage:                    vc.IsPage,
			HashedName:                vc.HashedName,
			PackagePrefix:             "",
			CanonicalGoPackagePath:    vc.CanonicalGoPackagePath,
			VirtualInstances:          convertVirtualInstances(vc.VirtualInstances),
			CollectionName:            vc.Source.CollectionName,
			VerifyGeneratedCode:       config.CompilerVerifyGeneratedCode,
			ModuleName:                s.resolver.GetModuleName(),
			IsEmail:                   vc.IsEmail,
			IsPdf:                     vc.IsPdf,
			EnablePrerendering:        s.enablePrerendering && !vc.IsEmail && !vc.IsPdf,
			EnableStaticHoisting:      config.CompilerEnableStaticHoisting,
			StripHTMLComments:         s.stripHTMLComments,
			EnableDwarfLineDirectives: s.enableDwarfLineDirectives,
		}

		artefact, err := s.generateSingleArtefact(ctx, request, annotationResult, vc)
		if err != nil {
			wctx.artefactMap.Store(request.SourcePath, err)
		} else {
			wctx.artefactMap.Store(request.SourcePath, artefact)
		}
	}
	return nil
}

// aggregateArtefactsAndDiagnostics collects all artefacts and diagnostics from
// the artefact map.
//
// Takes artefactMap (*syncmap.Map) which contains the generated artefacts or
// errors, keyed by source path.
// Takes projectResult (*annotator_dto.ProjectAnnotationResult) which provides
// the project-level diagnostics.
//
// Returns []*generator_dto.GeneratedArtefact which contains the collected
// artefacts.
// Returns []string which contains the canonical package paths of all
// components.
// Returns error when the diagnostics contain errors.
func (s *generatorService) aggregateArtefactsAndDiagnostics(
	ctx context.Context,
	artefactMap *syncmap.Map,
	projectResult *annotator_dto.ProjectAnnotationResult,
) ([]*generator_dto.GeneratedArtefact, []string, error) {
	ctx, l := logger_domain.From(ctx, log)
	var allArtefacts []*generator_dto.GeneratedArtefact
	var allDiagnostics []*ast_domain.Diagnostic
	var allPackagePaths []string

	allDiagnostics = append(allDiagnostics, projectResult.AllDiagnostics...)

	artefactMap.Range(func(key, value any) bool {
		sourcePath, ok := key.(string)
		if !ok {
			diagnostic := ast_domain.NewDiagnostic(ast_domain.Error, "Internal error: artefact map key is not a string", "", ast_domain.Location{}, "")
			allDiagnostics = append(allDiagnostics, diagnostic)
			return true
		}
		if err, isErr := value.(error); isErr {
			if semanticErr, ok := errors.AsType[*annotator_domain.SemanticError](err); ok {
				allDiagnostics = append(allDiagnostics, semanticErr.Diagnostics...)
			} else {
				diagnostic := ast_domain.NewDiagnostic(ast_domain.Error, "An unknown fatal error occurred during generation: "+err.Error(), "", ast_domain.Location{}, sourcePath)
				allDiagnostics = append(allDiagnostics, diagnostic)
			}
		} else if artefact, ok := value.(*generator_dto.GeneratedArtefact); ok {
			allArtefacts = append(allArtefacts, artefact)
			if artefact.Component != nil {
				allPackagePaths = append(allPackagePaths, artefact.Component.CanonicalGoPackagePath)
			}
		}
		return true
	})

	if ast_domain.HasErrors(allDiagnostics) {
		formattedErrors := annotator_domain.FormatAllDiagnostics(allDiagnostics, projectResult.AllSourceContents)
		l.Error("Project generation failed with errors.", logger_domain.Int("diagnostic_count", len(allDiagnostics)))
		_, _ = s.manifestBuilder.Build(allArtefacts)
		return nil, nil, fmt.Errorf("project generation failed:\n%s", formattedErrors)
	}

	return allArtefacts, allPackagePaths, nil
}

// buildManifestAndRegister builds the project manifest and adds the register
// file as an artefact.
//
// Takes allArtefacts ([]*generator_dto.GeneratedArtefact) which contains the
// generated artefacts to include in the manifest.
// Takes allPackagePaths ([]string) which lists the package paths for the
// register file.
//
// Returns *generator_dto.Manifest which contains the built project manifest.
// Returns []*generator_dto.GeneratedArtefact which includes the original
// artefacts plus the generated register file.
// Returns error when the manifest cannot be built or register file generation
// fails.
func (s *generatorService) buildManifestAndRegister(
	ctx context.Context,
	allArtefacts []*generator_dto.GeneratedArtefact,
	allPackagePaths []string,
) (*generator_dto.Manifest, []*generator_dto.GeneratedArtefact, error) {
	ctx, l := logger_domain.From(ctx, log)
	manifest, err := s.manifestBuilder.Build(allArtefacts)
	if err != nil {
		return nil, nil, fmt.Errorf("failed to build manifest: %w", err)
	}

	registerContent, err := s.registerEmitter.Generate(ctx, allPackagePaths)
	if err != nil {
		return nil, nil, fmt.Errorf("failed to generate register aggregator file: %w", err)
	}

	registerPath := paths.Join(s.baseDir, distDir, "piko_register.go")
	registerArtefact := &generator_dto.GeneratedArtefact{
		Content:       registerContent,
		SuggestedPath: registerPath,
		Result:        nil,
		Component:     nil,
		JSArtefactID:  "",
	}
	allArtefacts = append(allArtefacts, registerArtefact)
	l.Internal("Register aggregator file generated.")

	return manifest, allArtefacts, nil
}

