// NewGeneratorService creates a new, fully configured generator
// service.
//
// It takes all its dependencies as interfaces, adhering to the
// Hexagonal architecture.
//
// Takes ctx (context.Context) which provides the context for
// construction-time operations.
// Takes pathsConfig (GeneratorPathsConfig) which provides path settings.
// Takes i18nDefaultLocale (string) which is the default locale for i18n.
// Takes ports (GeneratorPorts) which provides the required service adapters.
// Takes opts (...GeneratorServiceOption) which provides optional configuration.
//
// Returns GeneratorService which is the configured service ready for use.
// Returns error when the distribution package cannot be created.
func NewGeneratorService(ctx context.Context, pathsConfig GeneratorPathsConfig, i18nDefaultLocale string, ports GeneratorPorts, opts ...GeneratorServiceOption) (GeneratorService, error) {
	options := &generatorServiceOptions{}
	for _, opt := range opts {
		opt(options)
	}

	if !options.inMemoryMode {
		if err := ensureDistPackageExists(ctx, pathsConfig.BaseDir, options.distSandbox, ports.SandboxFactory); err != nil {
			return nil, fmt.Errorf("ensuring dist package exists: %w", err)
		}
	}

	baseSandbox := ports.BaseSandbox
	if baseSandbox == nil && !options.inMemoryMode {
		var err error
		if ports.SandboxFactory != nil {
			baseSandbox, err = ports.SandboxFactory.Create("generator base dir", pathsConfig.BaseDir, safedisk.ModeReadWrite)
		} else {
			baseSandbox, err = safedisk.NewNoOpSandbox(pathsConfig.BaseDir, safedisk.ModeReadWrite)
		}
		if err != nil {
			return nil, fmt.Errorf("failed to create base sandbox for '%s': %w", pathsConfig.BaseDir, err)
		}
	}

	return &generatorService{
		baseDir:                   pathsConfig.BaseDir,
		fsWriter:                  ports.FSWriter,
		manifestEmitter:           ports.ManifestEmitter,
		coordinator:               ports.Coordinator,
		resolver:                  ports.Resolver,
		manifestBuilder:           NewManifestBuilder(pathsConfig, i18nDefaultLocale, ports.Resolver.GetBaseDir(), WithConfigSandbox(baseSandbox)),
		registerEmitter:           ports.RegisterEmitter,
		codeEmitterFactory:        ports.CodeEmitterFactory,
		collectionEmitter:         ports.CollectionEmitter,
		searchIndexEmitter:        ports.SearchIndexEmitter,
		pkJSEmitter:               ports.PKJSEmitter,
		i18nEmitter:               ports.I18nEmitter,
		actionGenerator:           ports.ActionGenerator,
		seoService:                ports.SEOService,
		baseSandbox:               baseSandbox,
		enablePrerendering:        options.enablePrerendering,
		stripHTMLComments:         options.stripHTMLComments,
		enableDwarfLineDirectives: options.enableDwarfLineDirectives,
	}, nil
}

// GetMainComponent finds the main virtual component for an annotation result.
//
// Takes result (*annotator_dto.AnnotationResult) which provides the annotated
// AST and virtual module data to search.
//
// Returns *annotator_dto.VirtualComponent which is the main component found.
// Returns error when result is nil, missing required data, or when the
// component cannot be found due to internal data problems.
func GetMainComponent(result *annotator_dto.AnnotationResult) (*annotator_dto.VirtualComponent, error) {
	if result == nil || result.AnnotatedAST == nil || result.AnnotatedAST.SourcePath == nil {
		return nil, errors.New("internal error: annotation result is missing required data to find main component")
	}

	sourcePath := *result.AnnotatedAST.SourcePath
	hashedName, ok := result.VirtualModule.Graph.PathToHashedName[sourcePath]
	if !ok {
		return nil, fmt.Errorf("internal error: inconsistency, could not find hash for source path '%s'", sourcePath)
	}

	vc, ok := result.VirtualModule.ComponentsByHash[hashedName]
	if !ok {
		return nil, fmt.Errorf("internal error: inconsistency, could not find virtual component for hash '%s'", hashedName)
	}

	return vc, nil
}

// extractCollectionItemsFromModule groups virtual page instances by their
// collection name.
//
// Takes vm (*annotator_dto.VirtualModule) which contains the components and
// instances to process.
//
// Returns map[string][]collection_dto.ContentItem which maps each collection
// name to its content items.
func extractCollectionItemsFromModule(vm *annotator_dto.VirtualModule) map[string][]collection_dto.ContentItem {
	collectionItems := make(map[string][]collection_dto.ContentItem)

	for _, vc := range vm.ComponentsByHash {
		if !isCollectionTemplate(vc) {
			continue
		}
		for _, instance := range vc.VirtualInstances {
			if item, ok := convertVirtualInstanceToContentItem(instance); ok {
				collectionItems[vc.Source.CollectionName] = append(collectionItems[vc.Source.CollectionName], item)
			}
		}
	}

	return collectionItems
}

// isCollectionTemplate checks if a component is a collection template with
// instances.
//
// Takes vc (*annotator_dto.VirtualComponent) which is the component to check.
//
// Returns bool which is true when the component has a collection name and at
// least one virtual instance.
func isCollectionTemplate(vc *annotator_dto.VirtualComponent) bool {
	return vc.Source.HasCollection && vc.Source.CollectionName != "" && len(vc.VirtualInstances) > 0
}

// convertVirtualInstanceToContentItem converts a VirtualPageInstance to a
// ContentItem by extracting page data, content AST, excerpt AST, and raw
// content from the instance's InitialProps.
//
// Takes instance (annotator_dto.VirtualPageInstance) which is the virtual page
// to convert.
//
// Returns collection_dto.ContentItem which contains the converted content data.
// Returns bool which is false if the instance has no InitialProps.
func convertVirtualInstanceToContentItem(instance annotator_dto.VirtualPageInstance) (collection_dto.ContentItem, bool) {
	if instance.InitialProps == nil {
		return collection_dto.ContentItem{}, false
	}

	item := collection_dto.ContentItem{
		ID:             "",
		Slug:           "",
		Locale:         "",
		TranslationKey: "",
		Metadata:       make(map[string]any),
		RawContent:     "",
		ContentAST:     nil,
		ExcerptAST:     nil,
		URL:            instance.Route,
		ReadingTime:    0,
		CreatedAt:      "",
		UpdatedAt:      "",
		PublishedAt:    "",
	}

	if pageData, ok := instance.InitialProps["page"].(map[string]any); ok {
		item.Metadata = pageData
	}
	if contentAST, ok := instance.InitialProps["contentAST"].(*ast_domain.TemplateAST); ok {
		item.ContentAST = contentAST
	}
	if excerptAST, ok := instance.InitialProps["excerptAST"].(*ast_domain.TemplateAST); ok {
		item.ExcerptAST = excerptAST
	}
	if rawContent, ok := instance.InitialProps["rawContent"].(string); ok {
		item.RawContent = rawContent
	}

	return item, true
}

// calculateWorkerCount finds the best number of workers to use based on the
// CPU count and the number of components.
//
// Takes componentCount (int) which is the number of components to process.
//
// Returns int which is the worker count, always at least 1 but never more than
// the CPU count.
func calculateWorkerCount(componentCount int) int {
	return cmp.Or(min(runtime.NumCPU(), componentCount), 1)
}

// logCodeEmissionDebug logs debug details about generated code.
//
// Takes ctx (context.Context) which carries logging context.
// Takes sourcePath (string) which is the path to the source file.
// Takes code ([]byte) which is the generated code to log.
func logCodeEmissionDebug(ctx context.Context, sourcePath string, code []byte) {
	_, l := logger_domain.From(ctx, log)
	l.Trace("Code emission complete",
		logger_domain.String("source", sourcePath),
		logger_domain.Bool("has_init", strings.Contains(string(code), "func init()")),
		logger_domain.Bool("has_register", strings.Contains(string(code), "RegisterASTFunc")),
		logger_domain.Bool("has_buildast", strings.Contains(string(code), "func BuildAST")),
		logger_domain.Int("code_length", len(code)))
}

// derivePagePath gets the relative page path from a source file path.
// For example, "/project/pages/checkout.pk" with baseDir "/project"
// returns "pages/checkout".
//
// Takes sourcePath (string) which is the full path to the source file.
// Takes baseDir (string) which is the base folder for working out relative
// paths.
//
// Returns string which is the relative path without the .pk extension.
func derivePagePath(sourcePath, baseDir string) string {
	absSource, absErr := filepath.Abs(sourcePath)
	absBase, baseErr := filepath.Abs(baseDir)

	if absErr == nil && baseErr == nil {
		relPath, err := filepath.Rel(absBase, absSource)
		if err == nil && !strings.HasPrefix(relPath, "..") {
			return strings.TrimSuffix(relPath, ".pk")
		}
	}

	relPath, err := filepath.Rel(baseDir, sourcePath)
	if err != nil || strings.HasPrefix(relPath, "..") {
		relPath = filepath.Base(sourcePath)
	}
	return strings.TrimSuffix(relPath, ".pk")
}

// ensureDistPackageExists checks if the dist directory and a placeholder
// generated.go file exist to ensure it is a valid Go package. If either is
// missing, it creates them.
//
// Takes ctx (context.Context) which provides the base context for logging.
// Takes baseDir (string) which is the absolute path to the project root.
// Takes sandbox (safedisk.Sandbox) which is an optional sandbox for filesystem
// operations. If nil, a sandbox is created from the base directory.
//
// Returns error when the sandbox cannot be created, the placeholder file
// status cannot be checked, or directory or file creation fails.
func ensureDistPackageExists(ctx context.Context, baseDir string, sandbox safedisk.Sandbox, factory safedisk.Factory) error {
	outputDistDir := filepath.Join(baseDir, distDir)
	placeholderFileName := "generated.go"

	baseSandbox := sandbox
	var shouldClose bool
	if baseSandbox == nil {
		var err error
		if factory != nil {
			baseSandbox, err = factory.Create("ensure dist package", baseDir, safedisk.ModeReadWrite)
		} else {
			baseSandbox, err = safedisk.NewNoOpSandbox(baseDir, safedisk.ModeReadWrite)
		}
		if err != nil {
			return fmt.Errorf("failed to create sandbox for base directory '%s': %w", baseDir, err)
		}
		shouldClose = true
	}
	if shouldClose {
		defer func() { _ = baseSandbox.Close() }()
	}

	placeholderRelPath := filepath.Join(distDir, placeholderFileName)
	_, l := logger_domain.From(context.WithoutCancel(ctx), log)
	_, err := baseSandbox.Stat(placeholderRelPath)
	if err == nil {
		l.Internal("Placeholder 'dist/generated.go' already exists.")
		return nil
	}

	if !errors.Is(err, fs.ErrNotExist) {
		return fmt.Errorf("failed to check status of placeholder file '%s': %w", placeholderRelPath, err)
	}

	l.Internal("Placeholder 'dist/generated.go' not found. Ensuring it and its parent directory exist.")

	if err := baseSandbox.MkdirAll(distDir, DirPermission); err != nil {
		return fmt.Errorf("failed to create dist directory at '%s': %w", outputDistDir, err)
	}

	if err := baseSandbox.WriteFile(placeholderRelPath, []byte(distPackageBoilerplate), FilePermission); err != nil {
		return fmt.Errorf("failed to write placeholder 'generated.go' in dist directory: %w", err)
	}

	l.Internal("Successfully created placeholder 'dist/generated.go'.")
	return nil
}

