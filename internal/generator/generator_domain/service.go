// generatorService implements the GeneratorService interface.
// It coordinates the Piko compilation pipeline by calling the annotator
// service first, then directing its adapters to emit code.
type generatorService struct {
	// fsWriter writes generated code files to the file system.
	fsWriter FSWriterPort

	// manifestEmitter writes compilation manifests.
	manifestEmitter ManifestEmitterPort

	// coordinator manages the annotation process across all entry points.
	coordinator coordinator_domain.CoordinatorService

	// resolver resolves Piko-style paths and provides module information.
	resolver resolver_domain.ResolverPort

	// registerEmitter creates the content for the register aggregator file.
	registerEmitter RegisterEmitterPort

	// codeEmitterFactory creates code emitters for output generation.
	codeEmitterFactory CodeEmitterFactoryPort

	// collectionEmitter creates static collection files from annotated content.
	collectionEmitter CollectionEmitterPort

	// searchIndexEmitter creates search indexes for collections; nil skips
	// search index generation.
	searchIndexEmitter SearchIndexEmitterPort

	// pkJSEmitter writes client-side JavaScript files for pages; nil means
	// no JavaScript output is created.
	pkJSEmitter PKJSEmitterPort

	// i18nEmitter creates the i18n FlatBuffer binary file; nil skips generation.
	i18nEmitter I18nEmitterPort

	// actionGenerator generates action code files; nil skips action generation.
	actionGenerator ActionGeneratorPort

	// manifestBuilder builds manifest files from the generated artefacts.
	manifestBuilder *ManifestBuilder

	// seoService creates SEO files for the project; nil turns off SEO creation.
	seoService SEOServicePort

	// baseSandbox provides sandboxed access to the project root directory
	// for file operations that are not routed through port interfaces.
	baseSandbox safedisk.Sandbox

	// baseDir is the absolute path to the project root directory.
	baseDir string

	// enablePrerendering controls whether static HTML is prerendered.
	enablePrerendering bool

	// stripHTMLComments removes HTML comments from generated output.
	stripHTMLComments bool

	// enableDwarfLineDirectives emits valid DWARF //line directives in
	// generated code so debuggers can map back to .pk files.
	enableDwarfLineDirectives bool
}

// GeneratorPorts groups all adapter ports required by the generator service.
// It follows the dependency injection pattern to reduce parameter count in
// constructors.
type GeneratorPorts struct {
	// FSWriter writes generated output to the file system.
	FSWriter FSWriterPort

	// ManifestEmitter writes the generated manifest data to its output
	// destination.
	ManifestEmitter ManifestEmitterPort

	// Coordinator distributes work across generators.
	Coordinator coordinator_domain.CoordinatorService

	// Resolver finds Go source files using path resolution.
	Resolver resolver_domain.ResolverPort

	// RegisterEmitter writes the register function for generated code.
	RegisterEmitter RegisterEmitterPort

	// CodeEmitterFactory creates code file emitters for each package.
	CodeEmitterFactory CodeEmitterFactoryPort

	// CollectionEmitter writes collection data files and Go
	// wrappers to the output.
	CollectionEmitter CollectionEmitterPort

	// SearchIndexEmitter outputs search index data for the site.
	SearchIndexEmitter SearchIndexEmitterPort

	// PKJSEmitter outputs pre-processed front-end JavaScript files.
	PKJSEmitter PKJSEmitterPort

	// I18nEmitter writes translation data to the output.
	I18nEmitter I18nEmitterPort

	// ActionGenerator creates action code files from the ActionManifest.
	ActionGenerator ActionGeneratorPort

	// SEOService provides search engine optimisation features.
	SEOService SEOServicePort

	// BaseSandbox provides sandboxed access to the project root directory for file
	// operations not routed through port interfaces. If nil in non-WASM mode, a
	// NoOpSandbox is created from the base directory.
	BaseSandbox safedisk.Sandbox

	// SandboxFactory creates sandboxes with validated paths. When set and
	// BaseSandbox is nil, the factory is used before falling back to
	// NewNoOpSandbox.
	SandboxFactory safedisk.Factory
}

