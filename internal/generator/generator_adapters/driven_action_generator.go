package generator_adapters

import (
	"context"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"

	"piko.sh/piko/internal/annotator/annotator_dto"
	"piko.sh/piko/internal/generator/generator_domain"
	"piko.sh/piko/internal/generator/generator_dto"
	"piko.sh/piko/internal/typegen/typegen_adapters"
	"piko.sh/piko/wdk/safedisk"
)

const (
	// actionDirPermission is the permission mode for directories (rwxr-xr-x).
	actionDirPermission fs.FileMode = 0750

	// actionFilePermission is the file permission mode (rw-r-----).
	actionFilePermission fs.FileMode = 0640
)

var _ generator_domain.ActionGeneratorPort = (*ActionGeneratorAdapter)(nil)

// ActionGeneratorAdapter implements ActionGeneratorPort by generating action
// code using the embedded emitters. It converts the ActionManifest from the
// annotator's auto-discovery into ActionSpec format and generates the action
// code files.
type ActionGeneratorAdapter struct {
	// registryEmitter generates Go registry code from action specifications.
	registryEmitter *ActionRegistryEmitter

	// wrapperEmitter generates Go wrapper code from action specifications.
	wrapperEmitter *ActionWrapperEmitter

	// typeScriptEmitter generates TypeScript code from action specifications.
	typeScriptEmitter *typegen_adapters.ActionTypeScriptEmitter

	// sandbox provides sandboxed filesystem access for writing generated files.
	sandbox safedisk.Sandbox
}

// ActionGeneratorOption configures an ActionGeneratorAdapter.
type ActionGeneratorOption func(*ActionGeneratorAdapter)

// NewActionGeneratorAdapter creates a new ActionGeneratorAdapter with the
// embedded emitters for generating action code.
//
// Takes opts (...ActionGeneratorOption) which configures the adapter.
//
// Returns *ActionGeneratorAdapter which is ready to generate action code.
func NewActionGeneratorAdapter(opts ...ActionGeneratorOption) *ActionGeneratorAdapter {
	a := &ActionGeneratorAdapter{
		registryEmitter:   NewActionRegistryEmitter(),
		wrapperEmitter:    NewActionWrapperEmitter(),
		typeScriptEmitter: typegen_adapters.NewActionTypeScriptEmitter(),
	}

	for _, opt := range opts {
		opt(a)
	}

	return a
}

// GenerateActions generates all action code artefacts from the manifest.
//
// This method converts the ActionManifest to ActionSpec format and generates:
//   - dist/actions/registry.go - action name to handler mapping
//   - dist/actions/wrappers.go - type-safe wrapper functions
//   - dist/ts/actions.gen.ts - TypeScript client types
//
// Takes ctx (context.Context) for cancellation.
// Takes manifest (*annotator_dto.ActionManifest) which contains the discovered
// actions from the actions/ directory.
// Takes outputDir (string) which is the project root directory for output.
//
// Returns error when code generation or file writing fails.
func (a *ActionGeneratorAdapter) GenerateActions(
	ctx context.Context,
	manifest *annotator_dto.ActionManifest,
	_ string,
	outputDir string,
) error {
	specs := ConvertManifestToSpecs(manifest)
	if len(specs) == 0 {
		return nil
	}

	registryCode, err := a.registryEmitter.EmitRegistry(ctx, specs)
	if err != nil {
		return fmt.Errorf("emitting registry: %w", err)
	}

	wrapperCode, err := a.wrapperEmitter.EmitWrappers(ctx, specs)
	if err != nil {
		return fmt.Errorf("emitting wrappers: %w", err)
	}

	tsCode, err := a.typeScriptEmitter.EmitTypeScript(ctx, specs)
	if err != nil {
		return fmt.Errorf("emitting TypeScript: %w", err)
	}

	actionHeader := generator_dto.AnalysisBuildConstraint + "/* Code generated by piko; DO NOT EDIT. */\n\n"

	if len(registryCode) > 0 {
		registryCode = append([]byte(actionHeader), registryCode...)
		registryPath := filepath.Join(outputDir, "dist", "actions", "registry.go")
		if err := a.writeFile(registryPath, registryCode); err != nil {
			return fmt.Errorf("writing registry: %w", err)
		}
	}

	if len(wrapperCode) > 0 {
		wrapperCode = append([]byte(actionHeader), wrapperCode...)
		wrapperPath := filepath.Join(outputDir, "dist", "actions", "wrappers.go")
		if err := a.writeFile(wrapperPath, wrapperCode); err != nil {
			return fmt.Errorf("writing wrappers: %w", err)
		}
	}

	if len(tsCode) > 0 {
		tsPath := filepath.Join(outputDir, "dist", "ts", "actions.gen.ts")
		if err := a.writeFile(tsPath, tsCode); err != nil {
			return fmt.Errorf("writing TypeScript: %w", err)
		}
	}

	return nil
}

// writeFile writes data to a file, creating parent directories as needed
// and resolving paths relative to the sandbox root when one is configured.
//
// Takes path (string) which specifies the file path to write to.
// Takes data ([]byte) which contains the content to write.
//
// Returns error when the directory cannot be created or the file cannot be
// written.
func (a *ActionGeneratorAdapter) writeFile(path string, data []byte) error {
	if a.sandbox != nil {
		relPath := a.sandbox.RelPath(path)
		directory := filepath.Dir(relPath)
		if err := a.sandbox.MkdirAll(directory, actionDirPermission); err != nil {
			return fmt.Errorf("creating directory %s: %w", directory, err)
		}
		return a.sandbox.WriteFile(relPath, data, actionFilePermission)
	}
	directory := filepath.Dir(path)
	if err := os.MkdirAll(directory, actionDirPermission); err != nil {
		return fmt.Errorf("creating directory %s: %w", directory, err)
	}
	return os.WriteFile(path, data, actionFilePermission)
}

// WithActionSandbox injects a sandbox for filesystem operations.
//
// Takes sandbox (safedisk.Sandbox) which provides safe filesystem access.
//
// Returns ActionGeneratorOption which configures the generator with the sandbox.
func WithActionSandbox(sandbox safedisk.Sandbox) ActionGeneratorOption {
	return func(a *ActionGeneratorAdapter) {
		a.sandbox = sandbox
	}
}
