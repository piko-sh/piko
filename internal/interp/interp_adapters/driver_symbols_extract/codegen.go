const (
	// generatedFileHeader is prepended to all generated files before the
	// package declaration.
	generatedFileHeader = "// Code generated by 'piko extract'. DO NOT EDIT.\n\n"

	// reflectPackage is the package name used in AST nodes for the reflect
	// import.
	reflectPackage = "reflect"
)

// wrapperMeta holds metadata about a generated wrapper function for
// registration in the init() symbol map.
type wrapperMeta struct {
	// OriginalName is the original function name from the source package.
	OriginalName string

	// FuncName is the generated wrapper function name.
	FuncName string
}

// symbolEntry pairs a symbol name with its AST expression for use in
// the generated init() map literal.
type symbolEntry struct {
	// expression is the AST expression for the symbol's reflect.ValueOf call.
	expression ast.Expr

	// name is the symbol name used as the map key.
	name string
}

// GenerateFile produces the Go source code for a single package's
// symbol file. For packages with generic configuration, it also
// generates dispatch wrapper functions.
//
// Takes extractedPackage (ExtractedPackage) which provides the extracted package
// metadata and symbols.
// Takes outputPackage (string) which specifies the Go package name
// for the generated file.
// Takes generatorConfig (PackageConfig) which provides the generic type
// configuration for wrapper generation.
//
// Returns the formatted Go source bytes or an error if generation
// fails.
func GenerateFile(extractedPackage ExtractedPackage, outputPackage string, generatorConfig PackageConfig) ([]byte, error) {
	alias := importAlias(extractedPackage)
	symbols := buildSymbolEntries(extractedPackage, alias)

	var wrapperDecls []*ast.FuncDecl
	var wrapperMetas []wrapperMeta
	if generatorConfig.IsGeneric() && len(extractedPackage.GenericFuncs) > 0 {
		var err error
		wrapperDecls, wrapperMetas, err = generateWrappers(extractedPackage, alias, generatorConfig)
		if err != nil {
			return nil, fmt.Errorf("generating wrappers for %s: %w", extractedPackage.ImportPath, err)
		}
	}

	if len(symbols) == 0 && len(wrapperDecls) == 0 {
		return nil, nil
	}

	mapEntries := buildMapEntries(symbols, wrapperMetas)
	initFunc := buildInitFuncAST(extractedPackage.ImportPath, mapEntries)

	var decls []ast.Decl
	for _, d := range wrapperDecls {
		decls = append(decls, d)
	}
	decls = append(decls, initFunc)

	fset := token.NewFileSet()
	file := &ast.File{
		Name:  goastutil.CachedIdent(outputPackage),
		Decls: decls,
	}

	goastutil.AddImport(fset, file, reflectPackage)
	if strings.Contains(extractedPackage.ImportPath, "/") {
		goastutil.AddNamedImport(fset, file, alias, extractedPackage.ImportPath)
	} else {
		goastutil.AddImport(fset, file, extractedPackage.ImportPath)
	}
	if len(wrapperDecls) > 0 {
		goastutil.AddImport(fset, file, "fmt")
	}

	formatted, err := goastutil.FormatAST(fset, file)
	if err != nil {
		return nil, fmt.Errorf("formatting generated code for %s: %w", extractedPackage.ImportPath, err)
	}

	header := generatedFileHeader
	if generatorConfig.BuildTag != "" {
		header = "// Code generated by 'piko extract'. DO NOT EDIT.\n\n//go:build " + generatorConfig.BuildTag + "\n\n"
	}
	return append([]byte(header), formatted...), nil
}

// OutputFileName returns the generated file name for a package.
// For example, "encoding/json" becomes "gen_encoding_json.go".
//
// Takes importPath (string) which specifies the Go import path to
// convert.
//
// Returns the generated file name with slashes replaced by
// underscores.
func OutputFileName(importPath string) string {
	safe := strings.ReplaceAll(importPath, "/", "_")
	return "gen_" + safe + ".go"
}

// GenerateTypesLoaderFile produces the Go source for the types_loader
// that uses go/importer.Default() to load *types.Package for generic
// packages.
//
// Takes genericPaths ([]string) which lists the import paths of
// generic packages to load.
// Takes outputPackage (string) which specifies the Go package name
// for the generated file.
//
// Returns the formatted Go source bytes or an error if generation
// fails.
func GenerateTypesLoaderFile(genericPaths []string, outputPackage string) ([]byte, error) {
	fset := token.NewFileSet()
	file := &ast.File{
		Name: goastutil.CachedIdent(outputPackage),
	}

	varDecl := &ast.GenDecl{
		Tok: token.VAR,
		Specs: []ast.Spec{
			&ast.ValueSpec{
				Names:  []*ast.Ident{goastutil.CachedIdent("TypesPackages")},
				Values: []ast.Expr{typesPackagesMakeExpr()},
			},
		},
	}

	pathElts := make([]ast.Expr, 0, len(genericPaths))
	for _, p := range genericPaths {
		pathElts = append(pathElts, goastutil.StrLit(p))
	}

	initFunc := buildTypesLoaderInitAST(pathElts)
	file.Decls = []ast.Decl{varDecl, initFunc}

	goastutil.AddImport(fset, file, "go/importer")
	goastutil.AddImport(fset, file, "go/types")

	formatted, err := goastutil.FormatAST(fset, file)
	if err != nil {
		return nil, fmt.Errorf("formatting types_loader: %w", err)
	}

	header := "// Code generated by 'piko extract'. DO NOT EDIT.\n\n//go:build !js || !wasm\n\n"
	return append([]byte(header), formatted...), nil
}

// GenerateTypesLoaderWASMFile produces the WASM stub for the types
// loader, providing an empty map since go/importer is unavailable in
// WASM.
//
// Takes outputPackage (string) which specifies the Go package name
// for the generated file.
//
// Returns the formatted Go source bytes or an error if generation
// fails.
func GenerateTypesLoaderWASMFile(_ []string, outputPackage string) ([]byte, error) {
	fset := token.NewFileSet()
	file := &ast.File{
		Name: goastutil.CachedIdent(outputPackage),
	}

	varDecl := &ast.GenDecl{
		Tok: token.VAR,
		Specs: []ast.Spec{
			&ast.ValueSpec{
				Names:  []*ast.Ident{goastutil.CachedIdent("TypesPackages")},
				Values: []ast.Expr{typesPackagesMakeExpr()},
			},
		},
	}

	file.Decls = []ast.Decl{varDecl}

	goastutil.AddImport(fset, file, "go/types")

	formatted, err := goastutil.FormatAST(fset, file)
	if err != nil {
		return nil, fmt.Errorf("formatting types_loader_wasm: %w", err)
	}

	header := "// Code generated by 'piko extract'. DO NOT EDIT.\n\n//go:build js && wasm\n\n"
	return append([]byte(header), formatted...), nil
}

// buildSymbolEntries filters typed symbols and builds symbolEntry
// values for each, using the given import alias.
//
// Takes extractedPackage (ExtractedPackage) which provides the extracted package
// with its symbols.
// Takes alias (string) which specifies the import alias for
// qualified references.
//
// Returns a slice of symbolEntry values for non-untyped symbols.
func buildSymbolEntries(extractedPackage ExtractedPackage, alias string) []symbolEntry {
	var symbols []symbolEntry
	for _, symbol := range extractedPackage.Symbols {
		if symbol.IsUntypedConst {
			continue
		}
		symbols = append(symbols, symbolEntry{
			name:       symbol.Name,
			expression: symbolExpressionAST(symbol, alias),
		})
	}
	return symbols
}

// buildMapEntries constructs the AST key-value expressions for the
// symbol map literal from both regular symbols and wrapper metas.
//
// Takes symbols ([]symbolEntry) which provides the regular symbol
// entries.
// Takes wrapperMetas ([]wrapperMeta) which provides the generated
// wrapper metadata.
//
// Returns a slice of AST key-value expressions for the map literal.
func buildMapEntries(symbols []symbolEntry, wrapperMetas []wrapperMeta) []ast.Expr {
	mapEntries := make([]ast.Expr, 0, len(symbols)+len(wrapperMetas))
	for _, s := range symbols {
		mapEntries = append(mapEntries, goastutil.KeyValueExpr(goastutil.StrLit(s.name), s.expression))
	}
	for _, wm := range wrapperMetas {
		mapEntries = append(mapEntries, goastutil.KeyValueExpr(
			goastutil.StrLit(wm.OriginalName),
			goastutil.CallExpr(
				goastutil.SelectorExpr(reflectPackage, "ValueOf"),
				goastutil.CachedIdent(wm.FuncName),
			),
		))
	}
	return mapEntries
}

// buildInitFuncAST constructs the init() function declaration that
// populates the Symbols map for a given import path.
//
// Takes importPath (string) which specifies the key for the Symbols
// map.
// Takes mapEntries ([]ast.Expr) which provides the key-value
// expressions for the map literal.
//
// Returns the AST function declaration for the init function.
func buildInitFuncAST(importPath string, mapEntries []ast.Expr) *ast.FuncDecl {
	initBody := &ast.AssignStmt{
		Lhs: []ast.Expr{
			&ast.IndexExpr{
				X:     goastutil.CachedIdent("Symbols"),
				Index: goastutil.StrLit(importPath),
			},
		},
		Tok: token.ASSIGN,
		Rhs: []ast.Expr{
			goastutil.CompositeLit(
				&ast.MapType{
					Key:   goastutil.CachedIdent("string"),
					Value: goastutil.SelectorExpr(reflectPackage, "Value"),
				},
				mapEntries...,
			),
		},
	}
	return goastutil.FuncDecl("init", nil, nil, goastutil.BlockStmt(initBody))
}

// symbolExpressionAST returns the AST expression for registering a
// symbol via reflect.ValueOf.
//
// Takes symbol (ExtractedSymbol) which provides the extracted symbol
// metadata.
// Takes alias (string) which specifies the import alias for the
// package reference.
//
// Returns the AST expression wrapping the symbol in reflect.ValueOf.
func symbolExpressionAST(symbol ExtractedSymbol, alias string) ast.Expr {
	qualified := goastutil.SelectorExpr(alias, symbol.Name)
	reflectValueOf := goastutil.SelectorExpr(reflectPackage, "ValueOf")

	switch symbol.Kind {
	case SymbolVar:
		return goastutil.CallExpr(
			goastutil.SelectorExprFrom(
				goastutil.CallExpr(reflectValueOf, goastutil.AddressExpr(qualified)),
				"Elem",
			),
		)

	case SymbolType:
		castExpr := &ast.CallExpr{
			Fun:  &ast.ParenExpr{X: goastutil.StarExpr(qualified)},
			Args: []ast.Expr{goastutil.CachedIdent("nil")},
		}
		return goastutil.CallExpr(reflectValueOf, castExpr)

	default:
		return goastutil.CallExpr(reflectValueOf, qualified)
	}
}

// importAlias returns the import alias to use for a package, handling
// collision avoidance with the reflect package.
//
// Takes extractedPackage (ExtractedPackage) which provides the extracted package
// metadata.
//
// Returns the alias string for use in import declarations.
func importAlias(extractedPackage ExtractedPackage) string {
	baseName := path.Base(extractedPackage.ImportPath)

	if !strings.Contains(extractedPackage.ImportPath, "/") {
		return baseName
	}

	alias := extractedPackage.Name
	if alias == reflectPackage {
		alias = "_reflect"
	}

	return alias
}

// buildTypesLoaderInitAST constructs the init() function that uses
// go/importer.Default() to populate TypesPackages.
//
// Takes pathElts ([]ast.Expr) which provides the AST string literal
// expressions for package paths.
//
// Returns the AST function declaration for the init function.
func buildTypesLoaderInitAST(pathElts []ast.Expr) *ast.FuncDecl {
	impDef := goastutil.DefineStmt("imp", goastutil.CallExpr(
		goastutil.SelectorExpr("importer", "Default"),
	))

	pkgAssign := goastutil.DefineStmtMulti([]string{"pkg", "err"}, goastutil.CallExpr(
		goastutil.SelectorExprFrom(goastutil.CachedIdent("imp"), "Import"),
		goastutil.CachedIdent("path"),
	))

	ifNoErr := goastutil.IfStmt(nil,
		&ast.BinaryExpr{
			X:  goastutil.CachedIdent("err"),
			Op: token.EQL,
			Y:  goastutil.CachedIdent("nil"),
		},
		goastutil.BlockStmt(
			goastutil.AssignStmt(
				&ast.IndexExpr{
					X:     goastutil.CachedIdent("TypesPackages"),
					Index: goastutil.CachedIdent("path"),
				},
				goastutil.CachedIdent("pkg"),
			),
		),
	)

	rangeStmt := &ast.RangeStmt{
		Key:   goastutil.CachedIdent("_"),
		Value: goastutil.CachedIdent("path"),
		Tok:   token.DEFINE,
		X: goastutil.CompositeLit(
			&ast.ArrayType{Elt: goastutil.CachedIdent("string")},
			pathElts...,
		),
		Body: goastutil.BlockStmt(pkgAssign, ifNoErr),
	}

	return goastutil.FuncDecl("init", nil, nil, goastutil.BlockStmt(impDef, rangeStmt))
}

// typesPackagesMakeExpr returns make(map[string]*types.Package).
//
// Returns the AST expression for the make call.
func typesPackagesMakeExpr() ast.Expr {
	return goastutil.CallExpr(
		goastutil.CachedIdent("make"),
		goastutil.MapType(
			goastutil.CachedIdent("string"),
			goastutil.StarExpr(goastutil.SelectorExpr("types", "Package")),
		),
	)
}

// typesDescriptor is the JSON structure for the types descriptor file.
type typesDescriptor struct {
	// ImportPaths lists the Go import paths included in the
	// descriptor.
	ImportPaths []string `json:"import_paths"`
}

// GenerateTypesDescriptorFile produces a JSON descriptor listing all
// extracted import paths. The descriptor enables piko bytecode to load
// types.Package objects for these packages at compile time.
//
// Takes importPaths ([]string) which lists the import paths to include.
//
// Returns the JSON bytes or an error if marshalling fails.
func GenerateTypesDescriptorFile(importPaths []string) ([]byte, error) {
	sorted := make([]string, len(importPaths))
	copy(sorted, importPaths)
	slices.Sort(sorted)

	descriptor := typesDescriptor{ImportPaths: sorted}
	output, err := pikojson.MarshalIndent(descriptor, "", "  ")
	if err != nil {
		return nil, fmt.Errorf("marshalling types descriptor: %w", err)
	}

	return append(output, '\n'), nil
}

// ReadTypesDescriptor parses a types descriptor JSON file and returns
// the import paths listed within it.
//
// Takes data ([]byte) which is the raw JSON content.
//
// Returns []string which lists the import paths from the descriptor.
// Returns error when the JSON is malformed.
func ReadTypesDescriptor(data []byte) ([]string, error) {
	var descriptor typesDescriptor
	if err := pikojson.Unmarshal(data, &descriptor); err != nil {
		return nil, fmt.Errorf("parsing types descriptor: %w", err)
	}
	return descriptor.ImportPaths, nil
}
