package lsp_adapters

import (
	"context"
	"fmt"
	"sync"

	"piko.sh/piko/internal/inspector/inspector_domain"
	"piko.sh/piko/internal/inspector/inspector_dto"
)

// memoryTypeDataProvider is a driven adapter that implements the
// TypeDataProvider port for the Inspector hexagon.
//
// It provides a simple, in-memory caching layer for the expensive type
// analysis results generated by the TypeInspectorManager. For a Language
// Server, an in-memory cache is ideal because its lifetime is tied to the
// editor session, and we want the fastest possible access without disk I/O.
//
// This implementation is thread-safe using a RWMutex to allow for concurrent
// reads while ensuring writes are serialised.
type memoryTypeDataProvider struct {
	// data holds the cached type data; nil means no data is stored.
	data *inspector_dto.TypeData

	// mu guards access to the data field.
	mu sync.RWMutex
}

var _ inspector_domain.TypeDataProvider = (*memoryTypeDataProvider)(nil)

// GetTypeData retrieves the cached type information.
//
// Takes cacheKey (string) which identifies the cached data to retrieve.
//
// Returns *inspector_dto.TypeData which contains the cached type information.
// Returns error when no data has been cached yet (cache miss).
//
// Safe for concurrent use. Uses a read lock to allow multiple goroutines
// to access the cache at the same time.
func (p *memoryTypeDataProvider) GetTypeData(_ context.Context, cacheKey string) (*inspector_dto.TypeData, error) {
	p.mu.RLock()
	defer p.mu.RUnlock()

	if p.data != nil {
		return p.data, nil
	}

	return nil, fmt.Errorf("cache miss for key '%s'", cacheKey)
}

// SaveTypeData saves the provided type information to the in-memory
// store, using a write-lock for atomicity and overwriting any
// previously cached data.
//
// Takes data (*inspector_dto.TypeData) which contains the type information
// to store.
//
// Returns error which is always nil for this implementation.
//
// Safe for concurrent use.
func (p *memoryTypeDataProvider) SaveTypeData(_ context.Context, _ string, data *inspector_dto.TypeData) error {
	p.mu.Lock()
	defer p.mu.Unlock()

	p.data = data
	return nil
}

// InvalidateCache removes a specific entry from the cache. For this simple
// in-memory implementation, it is functionally identical to ClearCache as
// there is only one "slot" for data.
//
// Returns error when the cache cannot be invalidated; always returns nil for
// this implementation.
//
// Safe for concurrent use.
func (p *memoryTypeDataProvider) InvalidateCache(_ context.Context, _ string) error {
	p.mu.Lock()
	defer p.mu.Unlock()

	p.data = nil
	return nil
}

// ClearCache removes all data from the in-memory store.
//
// Returns error which is always nil for this implementation.
//
// Safe for concurrent use.
func (p *memoryTypeDataProvider) ClearCache(_ context.Context) error {
	p.mu.Lock()
	defer p.mu.Unlock()

	p.data = nil
	return nil
}
